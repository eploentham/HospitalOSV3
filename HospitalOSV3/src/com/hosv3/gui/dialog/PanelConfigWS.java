    public void showDialog()
    {
        if(theJD==null)
            theJD = new JDialog(theUS.getJFrame());
        theJD.add(this);
        theJD.setSize(340, 160);
        Dimension screenSize = Toolkit.getDefaultToolkit().getScreenSize();
        theJD.setLocation((screenSize.width - theJD.getSize().width) / 2, (screenSize.height - theJD.getSize().height) / 2);
        theJD.setTitle("ตั้งค่า Webservice");
        theJD.setDefaultCloseOperation(JDialog.DO_NOTHING_ON_CLOSE);
        jTextFieldWSUrl.setText(theHC.theHO.theWSConfig.url);
        theJD.setModal(true);
        theJD.setVisible(true);
    }
