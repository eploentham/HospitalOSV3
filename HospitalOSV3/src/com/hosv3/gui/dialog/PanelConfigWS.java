    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        this.getWSConfig();
        int res = this.theHC.theSystemControl.saveWSConfig(theWSConfig);
        if(res == 3)
        {
            jTextFieldUserName.requestFocus();
            this.theUS.setStatus("กรุณากรอก Username",UpdateStatus.WARNING);
            return;
        }
        if(res == 4)
        {
            jTextFieldPassword.requestFocus();
            this.theUS.setStatus("กรุณากรอก Password",UpdateStatus.WARNING);
            return;
        }
        this.theJD.setVisible(false);
    }//GEN-LAST:event_jButton1ActionPerformed

