    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(theInType == null)
            theInType = new InType();
        theInType.showDialog(theUS);
        if(theNhsoSubInscl!=null)
        {
            int select = jTableVisitPayment.getSelectedRow();
            theMapNhsoSubInscl = new MapNhsoSubInscl();
            Plan p = (Plan) thePlan.get(select);
            theMapNhsoSubInscl.b_contract_plans_id = p.getObjectId();
            theMapNhsoSubInscl.f_nhso_sub_inscl_id = theNhsoSubInscl.getObjectId();
            theMapNhsoSubInscl.map_nhso_sub_inscl_type = theInType.type;
            theHC.theSystemControl.saveMapNhsoSubInscl(theMapNhsoSubInscl);
        }
        if(theNhsoMainInscl!=null)
        {
            int select = jTableVisitPayment.getSelectedRow();
            theMapNhsoMainInscl = new MapNhsoMainInscl();
            Plan p = (Plan) thePlan.get(select);
            theMapNhsoMainInscl.b_contract_plans_id = p.getObjectId();
            theMapNhsoMainInscl.f_nhso_main_inscl_id = theNhsoMainInscl.getObjectId();
            theMapNhsoMainInscl.map_nhso_main_inscl_type = theInType.type;
            theHC.theSystemControl.saveMapNhsoMainInscl(theMapNhsoMainInscl);
        }
        theJD.setVisible(false);
    }//GEN-LAST:event_jButton1ActionPerformed


