    private void selectThemeAction(String theme){
        this.theme = theme;
        try {
            if(theme.equals("Mac")){
                com.birosoft.liquid.LiquidLookAndFeel lq = new com.birosoft.liquid.LiquidLookAndFeel();
                UIManager.setLookAndFeel(lq);
                LiquidLookAndFeel.setLiquidDecorations(false);
                UIManager.put("ComboBoxUI","com.birosoft.liquid.LiquidComboBoxUI");
            }else if(theme.equals("Windows")){
                com.sun.java.swing.plaf.windows.WindowsLookAndFeel win = new com.sun.java.swing.plaf.windows.WindowsLookAndFeel();
                UIManager.setLookAndFeel(win);
                UIManager.put("ComboBoxUI","com.sun.java.swing.plaf.windows.WindowsComboBoxUI");
            }else if(theme.equals("Motif")){
                com.sun.java.swing.plaf.motif.MotifLookAndFeel motif = new com.sun.java.swing.plaf.motif.MotifLookAndFeel();
                UIManager.setLookAndFeel(motif);
                UIManager.put("ComboBoxUI","com.sun.java.swing.plaf.motif.MotifComboBoxUI");
            }else if(theme.equals("Metal")){
                javax.swing.plaf.metal.MetalLookAndFeel metal = new javax.swing.plaf.metal.MetalLookAndFeel();
                UIManager.setLookAndFeel(metal);
                UIManager.put("ComboBoxUI","javax.swing.plaf.metal.MetalComboBoxUI");
            }else{
                selectionPlastic(theme);
            }
            SwingUtilities.updateComponentTreeUI(this);
//            SwingUtilities.updateComponentTreeUI(theUS.getJFrame());
        } catch (UnsupportedLookAndFeelException ex) {
            ex.printStackTrace();
        }
//        setVisible(false);
//        DialogTheme theDialogTheme = new DialogTheme(theme,theUS,theHC);
//        theDialogTheme.setVisible(true);

        System.gc();
    }
    
