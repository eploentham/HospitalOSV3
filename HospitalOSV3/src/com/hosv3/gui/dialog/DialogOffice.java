    /**
     *  แสดง Fialoag
     * @param ส่งข้อมูลของ Object office มาให้
     * @return
     * @author
     * @date  01-04-2549
     */
    public static boolean showDialog(HosControl hc,UpdateStatus us,Office off)
    {
        DialogOffice dlg = new DialogOffice(hc,us,off);
        dlg.updateComboboxOG(off.getObjectId());
        dlg.setSize(300,400);
        dlg.setTitle(Constant.getTextBundle("สถานพยาบาล"));
        dlg.setLanguage("");
        Dimension screenSize = Toolkit.getDefaultToolkit().getScreenSize();
        dlg.setLocation((screenSize.width-dlg.getSize().width)/2, (screenSize.height-dlg.getSize().height)/2);
        dlg.setVisible(true);
        if(dlg.actionCommand)
        {
            if(dlg.theOfficeNow.getObjectId() == null )
            {
                us.setStatus("ไม่สามารถอ่านค่าสถานพยาบาลจากDialogได้", UpdateStatus.WARNING);
            }
            off.setObjectId(dlg.theOfficeNow.getObjectId());
            off.name = dlg.theOfficeNow.name;
            return true;
        }
        dlg=null;
        System.gc();
        return false;
    }
    
