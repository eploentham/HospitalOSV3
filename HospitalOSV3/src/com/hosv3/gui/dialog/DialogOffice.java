                /**
                 *  เรียกใช้เมื่อ Double-Click ที่รายการ
                 * @param
                 * @return
                 * @author
                 * @date  01-04-2549
                 */
                private void doubleClickList()
                {
                    try
                    {
                        // ถ้า Double-Click ก็ให้ Add เข้ารายการ Order เลย
                        long clickTime = System.currentTimeMillis();
                        long clickInterval = clickTime - firstClickTime;
                        
                        if(clickInterval < 400)
                        {
                            firstClickTime = 0;
                            jButtonOKActionPerformed(null);
                        }
                        else
                        {
                            firstClickTime = clickTime;
                        }
                    }
                    catch(Exception ex)
                    {
                        ex.printStackTrace(Constant.getPrintStream());
                    }
                }
                
                /**
                 *  อับเดตข้อมูลไปยัง Gui
                 * @param
                 * @return
                 * @author
                 * @date  01-04-2549
                 */
                public void updateOG()
                {
                    if(vOffice==null) return;
                    TaBleModel tm = new TaBleModel(column,vOffice.size());
                    for(int i=0;i<vOffice.size();i++)
                    {
                        Office pd = (Office)vOffice.get(i);
                        tm.setValueAt(pd.getCode(),i,0);
                        tm.setValueAt(pd.getName(),i,1);
                    }
                    this.jTable1.setModel(tm);
                    jTable1.getColumnModel().getColumn(0).setPreferredWidth(100);
                    jTable1.getColumnModel().getColumn(1).setPreferredWidth(500);
                }
                /**
                 *  อับเดตข้อมูลไปยัง Gui
                 * @param
                 * @return
                 * @author
                 * @date  01-04-2549
                 */
                public void updateComboboxOG(String off)
                {
                    if(off !=null && off.equals("")==false)
                    {
                        theOfficeNow = theHC.theLookupControl.readHospitalByCode(off);
                        if(theOfficeNow !=null)
                        {
                            String ch = theOfficeNow.changwat + "0000";
                            String am = theOfficeNow.changwat + theOfficeNow.ampur + "00";
                            Gutil.setGuiData(jComboBoxChangwat,ch);
                            Gutil.setGuiData(jComboBoxAmphur,am);
                            Gutil.setGuiData(jTable1,vOffice,off);
                        }
                    }
                    else
                    {
                        if(theHO.theSite != null)
                        {
                            Gutil.setGuiData(this.jComboBoxChangwat,this.theHO.theSite.changwat);
                            if(theHO.theSite.changwat !=null)
                            {
                                Gutil.setGuiData(this.jComboBoxAmphur,this.theHO.theSite.amphor);
                            }
                        }
                        else
                        {
                            JOptionPane.showMessageDialog(this, Constant.getTextBundle("ตาราง Site ไม่มีข้อมูล โปรแกรมจะกำหนดที่อยู่เป็นจังหวัดภูเก็ตให้แทน"), Constant.getTextBundle("เตือน"), JOptionPane.WARNING_MESSAGE);
                            Gutil.setGuiData(jComboBoxChangwat,"830000");
                            Gutil.setGuiData(jComboBoxAmphur,"830100");
                        }
                    }
                }
                /**
                 *  แสดง Fialoag
                 * @param ส่งข้อมูลของ Object office มาให้
                 * @return
                 * @author
                 * @date  01-04-2549
                 */
                public boolean showDialog(Office of)
                {
                    theOfficeNow = null;
                    actionCommand = false;
                    updateComboboxOG(of.getObjectId());
                    setSize(300,400);
                    setTitle(Constant.getTextBundle("สถานพยาบาล"));
                    setLanguage("");
                    Dimension screenSize = Toolkit.getDefaultToolkit().getScreenSize();
                    setLocation((screenSize.width-getSize().width)/2, (screenSize.height-getSize().height)/2);
                    setVisible(true);
                    if(actionCommand)
                    {
                        of.setObjectId(theOfficeNow.getObjectId());
                        of.name = theOfficeNow.name;
                        return true;
                    }
                    System.gc();
                    return false;
                }
                
