    private boolean setDataToObj()
    {   
        String temp ="";
        if(jCheckBox1.isSelected())
        {    temp  += ",A"; 
        }
        if(jCheckBox2.isSelected())
        {    temp  += ",B"; 
        }
        if(jCheckBox3.isSelected())
        {    temp  += ",C"; 
        }
        if(jCheckBox4.isSelected())
        {    temp  += ",D";    
        }     
        if(jCheckBox5.isSelected())
        {    temp  += ",E";         
        }
        if(jCheckBox6.isSelected())
        {    temp  += ",F";     
        }
        if(jCheckBox7.isSelected())
        {    temp  += ",G";     
        }     
        if(jCheckBox8.isSelected())
        {    temp  += ",H";     
        }
        if(jCheckBox9.isSelected())
        {    temp  += ",I";         
        }
        if(jCheckBox10.isSelected())
        {    temp  += ",J";           
        }   
        String hitch = "0";
        if(jCheckBox11.isSelected())
            hitch = "1";
        
        if(temp.length()==0)
            return false;
        
        theAccident.cost_detail = temp.substring(1);
        theAccident.hitch_constitution = hitch;
        return true;
    }
    
    private void check(String detail) {
        if(detail.equals("A")) {
            jCheckBox1.setSelected(true);
        } else if(detail.equals("B")) {
            jCheckBox2.setSelected(true);
        } else if(detail.equals("C")) {
            jCheckBox3.setSelected(true);
        } else if(detail.equals("D")) {
            jCheckBox4.setSelected(true);
        } else if(detail.equals("E")) {
            jCheckBox5.setSelected(true);
        } else if(detail.equals("F")) {
            jCheckBox6.setSelected(true);
        } else if(detail.equals("G")) {
            jCheckBox7.setSelected(true);
        } else if(detail.equals("H")) {
            jCheckBox8.setSelected(true);
        } else if(detail.equals("I")) {
            jCheckBox9.setSelected(true);
        } else if(detail.equals("J")) {
            jCheckBox10.setSelected(true);
        }
    }
    
