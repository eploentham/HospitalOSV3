    public void setItemV(Vector v)
    {
        theItemV = v;
        if(theItemV == null)
        {
            theItemV = new Vector();
        }
        TaBleModel tm =new TaBleModel(col_item,theItemV.size());
        for(int i=0;i<theItemV.size();i++)
        {
            Item tmp = (Item) theItemV.get(i);
            tm.setValueAt(tmp.common_name, i, 0);
        }
        jTable1.setModel(tm);
    }
