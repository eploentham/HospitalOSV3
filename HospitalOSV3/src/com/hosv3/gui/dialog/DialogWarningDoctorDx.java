    private void setDialog() 
    {
        this.setSize(250,110);        
        Toolkit thekit = this.getToolkit();        
        Dimension screenSize = Toolkit.getDefaultToolkit().getScreenSize();
        this.setLocation((screenSize.width-this.getSize().width)/2, (screenSize.height-this.getSize().height)/2);        
    }    
     
    public boolean showDialog() 
    {
        initData();
        this.setVisible(true);
        return false;
    }
    
    private void initData()
    {
        if("1".equals(theHO.theEmployee.warning_dx)) jCheckBoxWarning.setSelected(false);
        else jCheckBoxWarning.setSelected(true);
    }
    
    /**
     *@deprecated henbe มีการเขียนข้อมูลลง HO โดย GUI ซึ่งผิด pattern
     */
    private void setGOEmployee()
    {
        if(jCheckBoxWarning.isSelected()) theHO.theEmployee.warning_dx = "0"; 
        else theHO.theEmployee.warning_dx = "1";        
    }
    
    private void saveEmployee()
    {
        setGOEmployee();
        theSetupControl.saveEmployee(theHO.theEmployee);
    }

