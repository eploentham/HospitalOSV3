    private void setTableDrugReceive()
    {
        TaBleModel tm ;
        if(vOrderItemReceiveDrug != null)
        {
            tm= new TaBleModel(col_jTableReceiveDrug,vOrderItemReceiveDrug.size());
            tm.setEditingCol(3);
            qty_receive = new String[vOrderItemReceiveDrug.size()];
            for(int i=0;i<vOrderItemReceiveDrug.size();i++)
            {
               OrderItemReceiveDrug oird = (OrderItemReceiveDrug)vOrderItemReceiveDrug.get(i);         
               tm.setValueAt(oird.common_name ,i ,0);
               tm.setValueAt(oird.qty_dispense ,i ,1);
               tm.setValueAt(oird.qty_return ,i ,2);
               if(oird.qty_receive.equals("") || oird.qty_receive==null || oird.qty_receive.equals("null"))
                   tm.setValueAt("" ,i ,3); 
               else
                   tm.setValueAt(oird.qty_receive ,i ,3);
               qty_receive[i] = oird.qty_receive;
            }
        }
        else
        {
            tm= new TaBleModel(col_jTableReceiveDrug,0);
        }
        jTableReceiveDrug.setModel(tm); 
        jTableReceiveDrug.getColumnModel().getColumn(0).setPreferredWidth(200); 
        jTableReceiveDrug.getColumnModel().getColumn(1).setPreferredWidth(50);
        jTableReceiveDrug.getColumnModel().getColumn(1).setCellRenderer(TableRenderer.getRendererRight());
        jTableReceiveDrug.getColumnModel().getColumn(2).setPreferredWidth(50);
        jTableReceiveDrug.getColumnModel().getColumn(2).setCellRenderer(TableRenderer.getRendererRight());
        jTableReceiveDrug.getColumnModel().getColumn(3).setPreferredWidth(50);
        jTableReceiveDrug.getColumnModel().getColumn(3).setCellRenderer(TableRenderer.getRendererRight());
    }    
    
    /**
     *เอาค่าในตารางที่มีการแก้ไข เก็บใน OBJ ของ OrderItemReceiveDrug
     */
    public boolean saveOIRD()
    {
        boolean ret = true;
        String date_time = theHC.theLookupControl.getTextCurrentDateTime();
        for(int i=0;vOrderItemReceiveDrug!=null && i<vOrderItemReceiveDrug.size();i++)
        {
            OrderItemReceiveDrug orird = (OrderItemReceiveDrug)vOrderItemReceiveDrug.get(i);
            orird.time_receive = date_time;
            orird.receiver = theHO.theEmployee.getObjectId();
            orird.qty_receive = (String)jTableReceiveDrug.getValueAt(i,3); 
            if(qty_receive[i].equals(""))
            {
                boolean ret_in = theOrderControl.receiveReturnDrug(orird);
                if(!ret_in) ret = false;
            }
        }
        return ret;
    }   
    
    /**
     *list ค่าที่มีการคืนยามา show
     */
    private void showDataToTableReceiveDrug()
    {        
        vOrderItemReceiveDrug = theOrderControl
            .listOrderItemReceiveDrugByVisitID(theHO.theVisit.getObjectId());
        if(vOrderItemReceiveDrug==null) return;
        setTableDrugReceive();        
    }       
   
