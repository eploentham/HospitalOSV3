    private void getObjectFromVectorAccidentGroup()
    {
        int select = this.jTableList.getSelectedRow();
        if(select != -1)
        {
            if(vAccidentGroup!= null)
            {
                theAccidentGroup = (Accident)vAccidentGroup.get(select);
                panelAccident.setAccident(theAccidentGroup);
            }
        }
    }
    
