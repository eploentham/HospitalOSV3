    public Persistent getXPer() {
        
        if(theSequenceData==null)
            theSequenceData = new SequenceData();
       
        theSequenceData.name = jTextFieldName.getText();
        theSequenceData.pattern  = jTextFieldPattern.getText();
        theSequenceData.value = jTextFieldValue.getText();
        if(jCheckBoxActive.isSelected())
            theSequenceData.active = "1";
        else
            theSequenceData.active = "0";    
        return theSequenceData;
    }

    public void setXPer(Persistent x) {
        theSequenceData = (SequenceData)x;
        jTextFieldCode.setText(theSequenceData.getObjectId());
        jTextFieldName.setText(theSequenceData.name);
        
        String pattern = theSequenceData.pattern;
        if(theSequenceData.getObjectId().equals("an") || theSequenceData.getObjectId().equals("vn")) 
            pattern = theSequenceData.pattern.substring(1);

        jTextFieldPattern.setText(pattern);
        jTextFieldValue.setText(theSequenceData.value);
        jCheckBoxActive.setSelected(theSequenceData.active.equals("1"));
    }

