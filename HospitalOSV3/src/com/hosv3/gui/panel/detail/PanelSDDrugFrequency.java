    public DrugFrequency getTheDrugFrequency() {
        int ret =0;
        theDrugFrequency.drug_frequency_id = jTextFieldCode.getText();
        theDrugFrequency.description = jTextAreaDescription.getText();
        theDrugFrequency.factor = jTextFieldQty.getText();
        if(jCheckBox1.isSelected())
            theDrugFrequency.active = "1";
        else
            theDrugFrequency.active = "0";
        
        return theDrugFrequency;
    }

    public void setTheDrugFrequency(DrugFrequency item) {
        setEnabled(true);
        theDrugFrequency = item;
        jTextFieldCode.setText(theDrugFrequency.drug_frequency_id);
        jTextAreaDescription.setText(theDrugFrequency.description);
        jTextFieldQty.setText(theDrugFrequency.factor);
        if(theDrugFrequency.active!=null && theDrugFrequency.active.equals("1"))
            jCheckBox1.setSelected(true);
        else
            jCheckBox1.setSelected(false);
    }
    
