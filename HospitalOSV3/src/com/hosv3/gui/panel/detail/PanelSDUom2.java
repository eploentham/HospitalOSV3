   public void delDrugDosePrint(int[] index){
        for(int i=index.length;i>0;i--)
            vDrugDosePrint.remove(index[i-1]);
        setDrugDosePrintV(vDrugDosePrint);
    }
    public void addDrugDosePrint(DrugDosePrint ddp){
        if(vDrugDosePrint==null)
            vDrugDosePrint = new Vector();
        this.vDrugDosePrint.add(ddp);
        setDrugDosePrintV(vDrugDosePrint);
    }
    private void setDrugDosePrintV(Vector vc){
        vDrugDosePrint = vc;
        if(vDrugDosePrint==null)
            vDrugDosePrint = new Vector();
        TaBleModel tm = new TaBleModel(new String[]{"ตัวเลข","ข้อความ"},vDrugDosePrint.size());
        for(int i=0 ;i<vDrugDosePrint.size();i++)
        {
            DrugDosePrint of = (DrugDosePrint)vDrugDosePrint.get(i);
            tm.setValueAt(of.item_drug_dose_value,i,0);
            tm.setValueAt(of.item_drug_dose_description,i,1);
        }
        jTableDrugDose.setModel(tm);
        jTableDrugDose.getColumnModel().getColumn(0).setPreferredWidth(40); 
        jTableDrugDose.getColumnModel().getColumn(1).setPreferredWidth(100); 
    }
    
    
