    public Uom getTheUom() {
        theUom.uom_id = jTextFieldCode.getText();
        theUom.description = jTextFieldName.getText();
        if(jCheckBox1.isSelected())
                theUom.active = "1";
        else    theUom.active = "0"; 
        return theUom;
    }

    public void setTheUom(Uom item) {
        theUom = item;
        jTextFieldCode.setText(theUom.uom_id);
        jTextFieldName.setText(theUom.description);
        if(theUom.active!=null&&theUom.active.equals("1"))
            jCheckBox1.setSelected(true);
        else
            jCheckBox1.setSelected(false);
        this.jLabelUOMPrint1.setText(getTheUom().description);
        this.jLabelUOMPrint2.setText(getTheUom().description);
        this.setDrugDosePrintV(theLookupControl.listDrugDosePrintByUom(getTheUom().getObjectId()));
    }

