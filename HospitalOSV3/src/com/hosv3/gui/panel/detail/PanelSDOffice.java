    public Persistent getXPer() 
    {
       if(theOffice==null)
       {
            theOffice = new Office();
            theOffice.minis = "";
            theOffice.dep = "";
            theOffice.off_type = "";
            theOffice.specific = "";
       }
        theOffice.pk_field = jTextFieldCode.getText();
        theOffice.name = jTextFieldOffName.getText();
        theOffice.off_name1 = jTextAreaFullName.getText();
        theOffice.off_name2 = jTextFieldDepartment.getText();
        theOffice.bed = jTextFieldBed.getText();
        theOffice.moo = jTextFieldMoo.getText();
        if((Gutil.getGuiData(jComboBoxChangwat)==null)||(Gutil.getGuiData(jComboBoxChangwat).equals("")))
        {  
        theOffice.changwat="00";
        }
        else
        {
         theOffice.changwat = Gutil.getGuiData(jComboBoxChangwat).substring(0,2);
        }
        if((Gutil.getGuiData(jComboBoxAmphur)== null)||(Gutil.getGuiData(jComboBoxAmphur).equals("")))
        {
          theOffice.ampur ="00";
        }
        else
        {
            theOffice.ampur = Gutil.getGuiData(jComboBoxAmphur).substring(2,4);
        }
        if((Gutil.getGuiData(jComboBoxTambon)==null)||(Gutil.getGuiData(jComboBoxTambon).equals("")))
        {   
         theOffice.tambon = "00";
        }
        else
        {
            theOffice.tambon = Gutil.getGuiData(jComboBoxTambon).substring(4,6);
        }
        return theOffice;
    }

    public void setXPer(Persistent x) 
    {
        theOffice = (Office)x;
        jTextFieldCode.setText(theOffice.getObjectId());
        jTextFieldOffName.setText(theOffice.name);
        jTextAreaFullName.setText(theOffice.off_name1);
        jTextFieldDepartment.setText(theOffice.off_name2);
        jTextFieldBed.setText(theOffice.bed);
        jTextFieldMoo.setText(theOffice.moo);
        ComboboxModel.setCodeComboBox(jComboBoxChangwat, theOffice.changwat+"0000");
        if(theOffice.changwat != null)
        if(!ComboboxModel.setCodeComboBox(jComboBoxAmphur, theOffice.changwat+theOffice.ampur+"00"))
        {   jComboBoxAmphur.removeAllItems();
            comboboxaddressamp.clear();
        }
        selectAmpor();
        //ComboboxModel.setCodeComboBox(jComboBoxTambon, theOffice.changwat+theOffice.ampur+theOffice.tambon);
        if(theOffice.tambon !=null)
        if(!ComboboxModel.setCodeComboBox(jComboBoxTambon, theOffice.changwat+theOffice.ampur+theOffice.tambon))
        {   
            jComboBoxTambon.removeAllItems();
            comboboxaddresstmb.clear();
        } 
        setEnableAll(true);
    }

