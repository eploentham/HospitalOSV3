    private void setItemV(Vector voffice,int off)
    {
        vItem = voffice;
        int count = offset;
        int old = prev;
        if(vItem != null && vItem.size() != 0)
        {
            
            if(off == 1)//กดปุ่ม next
               prev += count;
            else //กดปุ่ม previous
               prev -= count;
            if(prev>vItem.size())
                prev = old;
            if(prev<0)
                prev = 0;
            
            TaBleModel tm = new TaBleModel(col,count);
            tm.setEditingCol(3);
            for(int i=0 ;i<count && i+prev<vItem.size();i++)
            {
                String[] of = (String[])vItem.get(i+prev);
                tm.setValueAt(of[1],i,0);
                tm.setValueAt(of[3],i,1);
                tm.setValueAt(of[5],i,2);
                tm.setValueAt(of[6],i,3);
            }
            jTable1.setModel(tm);
        }
        jTable1.getColumnModel().getColumn(0).setPreferredWidth(300); // ชื่อสามัญสำหรับ รพ.ทั่วไป, ชื่อการค้า สำหรับศูนย์โรคตา
        jTable1.getColumnModel().getColumn(1).setPreferredWidth(120); // วันที่
        jTable1.getColumnModel().getColumn(2).setPreferredWidth(60); // ราคาเดิม
        jTable1.getColumnModel().getColumn(3).setPreferredWidth(60); // ราคาใหม่
        jTable1.getColumnModel().getColumn(2).setCellRenderer(TableRenderer.getRendererRight()); // ราคาเดิม
        jTable1.getColumnModel().getColumn(3).setCellRenderer(TableRenderer.getRendererRight()); // ราคาใหม่
    } 
    
