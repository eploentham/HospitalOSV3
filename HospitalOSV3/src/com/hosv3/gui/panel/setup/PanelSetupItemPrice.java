    public Vector getItemPrice(){
        for(int i=0;i<offset && prev+i<vItem.size();i++){
            String[] itemprice = (String[])vItem.get(prev+i);
            itemprice[6] = (String)jTable1.getValueAt(i,3);
        }
        return vItem;
    }
