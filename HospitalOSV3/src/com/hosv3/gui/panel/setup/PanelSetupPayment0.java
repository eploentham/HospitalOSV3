    /**ทำการค้นหา item ตามกลุ่มหรือ ตาม ชื่อที่ต้องการค้นหา*/
    private void searchItemGroup()
    {   //pu : 25/07/2549 : กำหนดค่า Index ให้กับหน้าที่ต้องการแสดงรายการตรวจรักษา
        String itemname= jTextFieldSCode.getText();
        String date_from = null;
        String date_to = null;
        if(this.jCheckBoxSearchDate.isSelected()){
            date_from = this.dateComboBoxFrom.getText();
            date_to = this.dateComboBoxTo.getText();
        }
        prev = 0;
        vItem = theSetupControl.listPatientRemain(false,itemname,date_from,date_to);
        setItemV(vItem, 0);
    }
    private void setItemV(Vector voffice,int off)
    {
        vItem = voffice;
        int count = offset;
        int old = prev;
            
            if(off == 1)//กดปุ่ม next
               prev += count;
            else //กดปุ่ม previous
               prev -= count;
            if(prev>vItem.size())
                prev = old;
            if(prev<0)
                prev = 0;
            
             prev = 0;
            TaBleModel tm = new TaBleModel(col,vItem.size());
            for(int i=0 ; i<vItem.size();i++)
            {
                String[] of = (String[])vItem.get(i+prev);
                tm.setValueAt(new Integer(i),i,0);
                tm.setValueAt(" "+of[1],i,1);
                tm.setValueAt(of[2],i,2);
                tm.setValueAt(of[3],i,3);
                tm.setValueAt(Integer.valueOf(of[4]),i,4);
                tm.setValueAt(" "+of[5].substring(0,10),i,5);
            }
            jTable1.setModel(tm);
            jTable1.getColumnModel().getColumn(0).setPreferredWidth(40); // ชื่อสามัญสำหรับ รพ.ทั่วไป, ชื่อการค้า สำหรับศูนย์โรคตา
            jTable1.getColumnModel().getColumn(1).setPreferredWidth(120); // ชื่อสามัญสำหรับ รพ.ทั่วไป, ชื่อการค้า สำหรับศูนย์โรคตา
            jTable1.getColumnModel().getColumn(2).setPreferredWidth(120); // วันที่
            jTable1.getColumnModel().getColumn(3).setPreferredWidth(120); // ราคาเดิม
            jTable1.getColumnModel().getColumn(4).setPreferredWidth(60); // ราคาใหม
            jTable1.getColumnModel().getColumn(5).setPreferredWidth(120); // ราคาใหม่่
            jTable1.getColumnModel().getColumn(4).setCellRenderer(TableRenderer.getRendererRight()); // ราคาใหม่
    } 
    
