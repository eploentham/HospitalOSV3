 public void showSite()
 {    
      Site site = theLookupControl.readSite();
      if(site== null)
      {
          save = 1;
      }
      else
      {
          save = 0;
          Gutil.setGuiData(jTextFieldCode, site.off_id);
          Gutil.setGuiData(jTextFieldOffName, site.off_name);
          Gutil.setGuiData(jTextFieldFullName, site.full_name);
          Gutil.setGuiData(jTextFieldPostCode, site.postcode);
          Gutil.setGuiData(jTextFieldAdmin, site.admin);
          Gutil.setGuiData(jComboBoxOPDCard, site.opd_type);
          Gutil.setGuiData(jTextFieldHN, site.hn_for_drug_fund);
          Gutil.setGuiData(jTextFieldAddress, site.address);
          try
          {
              Gutil.setGuiData(jTextFieldChangwatCode, site.changwat.substring(0,2));
              Gutil.setGuiData(jTextFieldAmphurCode, site.amphor.substring(2,4));
              Gutil.setGuiData(jTextFieldDistrictCode, site.tambon.substring(4,6));
          }
          catch(Exception ex)
          {}
          Gutil.setGuiData(jComboBoxChangWat, site.changwat);
          Gutil.setGuiData(jComboBoxAmphur, site.amphor);
          Gutil.setGuiData(jComboBoxDistrict, site.tambon);
          Gutil.setGuiData(jTextFieldMoo, site.village);
          Gutil.setGuiData(jTextFieldTel, site.tel);
                  
      }      
       site = null;
 }
 public void saveSite()
 {
     Site site = theHO.theSite;
     site.off_id = Gutil.getGuiData(jTextFieldCode);
     site.off_name =Gutil.getGuiData(jTextFieldOffName);
     site.full_name = Gutil.getGuiData(jTextFieldFullName);
     site.admin =Gutil.getGuiData(jTextFieldAdmin);
     site.postcode = Gutil.getGuiData(jTextFieldPostCode);
     site.opd_type =Gutil.getGuiData(jComboBoxOPDCard);
     site.address = Gutil.getGuiData(jTextFieldAddress);
     site.village = Gutil.getGuiData(jTextFieldMoo);
     site.changwat = Gutil.getGuiData(jComboBoxChangWat);
     site.amphor = Gutil.getGuiData(jComboBoxAmphur);
     site.tambon = Gutil.getGuiData(jComboBoxDistrict);
     site.hn_for_drug_fund = Gutil.getGuiData(jTextFieldHN);
     site.tel = Gutil.getGuiData(jTextFieldTel);
     theSetupControl.saveSite(site);
     showSite();
     
 }
 
