    public void updateOGEmployee(Employee bgi2)
    {
        theEmployee2 = bgi2;
        boolean is_user = theHO.theEmployee.getObjectId().equals(theEmployee2.getObjectId());
        boolean is_admin = theHO.theEmployee.authentication_id.equals(Authentication.ADMIN);
        setEnableAll(is_user || is_admin);
        boolean is_ok = bgi2.getObjectId()!=null;
        jButtonEditPasswd.setEnabled(is_ok && (is_user || is_admin));
        jTextFieldFirstName.setText(bgi2.fname);
        jTextFieldLastName.setText(bgi2.lname);
        jTextFieldEmCode.setText(bgi2.employee_no);
        ComboboxModel.setCodeComboBox(jComboBoxAuthen, bgi2.authentication_id);
        ComboboxModel.setCodeComboBox(jComboBoxClinic, bgi2.default_service_id);
        
        //amp :21/02/2549
        if("3".equals(bgi2.authentication_id)) 
        {
            jCheckBoxWarningDx.setVisible(true);
            if("1".equals(bgi2.warning_dx)) jCheckBoxWarningDx.setSelected(true);
            else jCheckBoxWarningDx.setSelected(false);
        }
        else 
        {
            jCheckBoxWarningDx.setVisible(false);
        }
        
        boolean has_tab = !bgi2.default_tab.equals("");
        if(has_tab)
            ComboboxModel.setCodeComboBox(jComboBoxTab, bgi2.default_tab);
        jCheckBoxDefaultTab.setSelected(has_tab);
        jComboBoxTab.setEnabled(has_tab);
        
        if(!theEmployee2.last_login.equals("")|| !theEmployee2.last_login.equalsIgnoreCase("null"))
                jTextFieldLastLogin.setText(DateUtil.convertFieldDate(bgi2.last_login));  
        else    jTextFieldLastLogin.setText("");
        if((!bgi2.last_logout.equalsIgnoreCase("null"))||(!bgi2.last_logout.equals("")))
                jTextFieldLastLogout.setText(DateUtil.convertFieldDate(bgi2.last_logout));       
        else    jTextFieldLastLogout.setText("");
        if(bgi2.active.equals("1"))
                jCheckBox1.setSelected(true);
        else    jCheckBox1.setSelected(false);
        
    }     
    private void searchEmployee()
    {   
        next = 0;
        prev = 0;
        String search = jTextFieldSCode.getText();
        String active = "0";
        if(jCheckBoxS.isSelected())
            active = "1";
        employee =  theSetupControl.listEmployeeSetup(search, active,null);      
        setTable(employee,1);
    }    
    private void setTable(Vector voffice,int off)
    {      
        Employee of = new Employee();  
        int count = offset;
        int p =0;
        int n =0;
        int c =0;
        if(voffice != null)
        {
            if(off == 1)
            {   
                p = prev;
                n = next;        
                prev = next;
                next = next + offset;
                if(next >= employee.size())
                {   
                    next = employee.size();
                    count = next - prev;
                }
                if(count == 0)
                {   
                    prev = p;
                    next = n;
                    count = n - p;
                }
            }
            else
            {   
                next = prev;
                prev = prev - offset;
                if(prev <=0)
                {    
                    prev = 0;
                    next = offset;
                }
                if(next >= employee.size())
                {   
                    next= employee.size();
                    count = next;
                }
            }
            TaBleModel tm = new TaBleModel(col,count);
            for(int i=0 ;i<count;i++)
            {  
                of = (Employee)voffice.get(i+prev);        
                tm.setValueAt(of.employee_id,i,0);
                tm.setValueAt(of.fname + " " + of.lname,i,1);
            }
            jTable1.setModel(tm);          
        }
        else
        {   
            TaBleModel tm = new TaBleModel(col,0);
            jTable1.setModel(tm);
        }      
    }  
  
    private void saveEmployee()
    {  
        theEmployee2.employee_id = jTextFieldCode.getText();
        theEmployee2.fname = jTextFieldFirstName.getText();
        theEmployee2.lname = jTextFieldLastName.getText();
        theEmployee2.employee_no = jTextFieldEmCode.getText();      
        theEmployee2.level_id = "";
        theEmployee2.rule_id = "";
        theEmployee2.default_service_id = ComboboxModel.getCodeComboBox(jComboBoxClinic);
//        theEmployee2.authentication_id_tmp = ComboboxModel.getCodeComboBox(jComboBoxAuthen);
        theEmployee2.authentication_id = theEmployee2.authentication_id + "-" + ComboboxModel.getCodeComboBox(jComboBoxAuthen);
//        String authentication_id_tmp = ComboboxModel.getCodeComboBox(jComboBoxAuthen);
         //amp:21/02/2549
        if("3".equals(theEmployee2.authentication_id)) 
        {
            if(jCheckBoxWarningDx.isSelected()) theEmployee2.warning_dx = "1";
            else theEmployee2.warning_dx = "0";            
        }
        else theEmployee2.warning_dx = "0";
        
        if(jCheckBoxDefaultTab.isSelected())
                theEmployee2.default_tab = ComboboxModel.getCodeComboBox(jComboBoxTab);
        else    theEmployee2.default_tab = "";
        if(jCheckBox1.isSelected())
                theEmployee2.active = "1";
        else    theEmployee2.active = "0"; 
     
        int ret = theSetupControl.saveEmployee(theEmployee2);
        if(ret <= 0)
            return;
        if(ret ==3)
        {
            updateOGEmployee(theEmployee2);
            return;
        }
        setEnableAll(false);
        jTable1.clearSelection();
        searchEmployee();
        refreshDepPanel();
    }
    
