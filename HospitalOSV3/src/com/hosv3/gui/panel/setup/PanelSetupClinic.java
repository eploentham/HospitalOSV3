    private void selectClinic()
    {   setEnableAll(true);
        saved = 0;
        int row = jTable1.getSelectedRow();
        String of = (String)jTable1.getValueAt(row, 0);
        jTextFieldCode.setText(of);
        jTextAreaName.setText((String)jTable1.getValueAt(row, 1));
        Clinic bgi = new Clinic();
        for(int i =0 ; i< clinic.size() ; i++)
        {           
             if(of == ((Clinic)clinic.get(i)).clinic_id)
                bgi = (Clinic)clinic.get(i);
        }
        theClinic = bgi;
        if(theClinic.active.equals("1"))
            jCheckBox1.setSelected(true);
        else
            jCheckBox1.setSelected(false);
    }
    private void searchClinic()
    {   next = 0;
        prev = 0;
        String search = jTextFieldSCode.getText();
        String active = "0";
        if(jCheckBoxS.isSelected())
            active = "1";
       clinic =  theSetupControl.listClinicAll(search, active);
        setTable(clinic,1);
    }
    private void setTable(Vector voffice,int off)
    {  
        Clinic of = new Clinic();
        int count = offset;
        int p =0;
        int n =0;
        int c =0;
        if(voffice != null)
        {
            if(off == 1)
            {   p = prev;
                n = next;
                prev = next;
                next = next + offset;
                if(next >= clinic.size())
                {   next = clinic.size();
                    count = next - prev;
                }
                if(count == 0)
                {   prev = p;
                    next = n;
                    count = n - p;
                }
            }
            else
            {   next = prev;
                prev = prev - offset;
                if(prev <=0)
                {    prev = 0;
                    next = offset;
                }
                if(next >= clinic.size())
                {   next= clinic.size();
                    count = next;
                }
            }
            TaBleModel tm = new TaBleModel(col,count);
            for(int i=0 ;i<count;i++)
            {  
                of = (Clinic)voffice.get(i+prev);
                 tm.setValueAt(of.clinic_id,i,0);
                tm.setValueAt(of.name,i,1);
            }
            jTable1.setModel(tm);
        }
        else
        {   TaBleModel tm = new TaBleModel(col,0);
            jTable1.setModel(tm);
        }
         setTableListDefault();
    }    

