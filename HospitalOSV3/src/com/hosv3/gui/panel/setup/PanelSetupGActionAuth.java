    private void jTree1ValueChanged(javax.swing.event.TreeSelectionEvent evt) {//GEN-FIRST:event_jTree1ValueChanged
        if(theAuthentication==null || theAuthentication.getObjectId()==null) {
            theUS.setStatus("กรุณาเลือกสิทธิการใช้งาน",UpdateStatus.WARNING);
            return;
        }
        curr_node = (DefaultMutableTreeNode)
        this.jTree1.getLastSelectedPathComponent();
        try{
            curr_gaa = (GActionAuth)curr_node.getUserObject();
        }catch(Exception e){
            theUS.setStatus("กรุณาเลือกจากรายการที่ย่อยที่สุด",UpdateStatus.WARNING);
            return;
        }
        if (curr_gaa == null){
            theUS.setStatus("กรุณาเลือกจากรายการที่ย่อยที่สุด",UpdateStatus.WARNING);
            return;
        }
        this.jCheckBoxRead.setSelected(curr_gaa.isRead());
        this.jCheckBoxWrite.setSelected(curr_gaa.isWrite());
        theUS.setStatus(curr_gaa.toString(),UpdateStatus.COMPLETE);
    }//GEN-LAST:event_jTree1ValueChanged

