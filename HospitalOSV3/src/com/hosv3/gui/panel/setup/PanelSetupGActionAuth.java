    private void setAuthentication(Authentication authentication) {

        ////////////////////////////////////////////////////////////////
        theAuthentication = authentication;
        if(theAuthentication==null){
            theAuthentication = new Authentication();
        }
        jTextFieldCode.setText(theAuthentication.getObjectId());
        jTextAreaName.setText(theAuthentication.description);
        ////////////////////////////////////////////////////////////////
        theGActionAuthV = theLookupControl.listGActionAuthByAid(theAuthentication.getObjectId());
        if(theGActionAuthV.isEmpty() || theGActionAuthV.size()<50){
            theGActionAuthV = theLookupControl.listGActionAuthByAid(theAuthentication.getObjectId());
            for(int i=0;i<theGActionAuthV.size();i++){
                GActionAuth gaa = (GActionAuth)theGActionAuthV.get(i);
                gaa.setObjectId(null);
                gaa.auth_id = theAuthentication.getObjectId();
                gaa.auth_name = jTextAreaName.getText();
            }
        }
        this.jTree1.setModel(GActionAuthV.getTreeModel(theGActionAuthV));
    }
