    private void insertDrugFrequency()
    {   saved = 1;
        setEnabled(true);
        jTextFieldCode.requestFocus();
        //jTextAreaDescription.setEnabled(true);
        clearAll();
    }
    private void deleteDrugFrequency()
    {
        int ret = 0;
        ret = theSetupControl.deleteDrugFrequency(theDrugFrequency);
        if(ret > 0)
        {
            theUS.setStatus("ลบช่วงเวลาการใช้ยาเสร็จสิ้น", theUS.COMPLETE);
            setEnabled(false);
            clearAll();
            //pu : 25/08/2549 : เก็บ Index ปัจจุบันของหน้ารายการที่กำลังบันทึก
            int count = next - prev;
            this.curNext = next - count;
            this.curPrev = prev - offset;
            searchDrugFrequency();
        }
    }
    private void clearAll()
    {   jTextFieldCode.setText("");
        jTextAreaDescription.setText("");
        jTextFieldQty.setText("");
        jCheckBox1.setSelected(true);
        theDrugFrequency = new DrugFrequency();
    }
