    private void selectGuide()
    {   
        setEnableAll(true);
        saved = 0;
        int row = jTable1.getSelectedRow();
        String of = (String)jTable1.getValueAt(row, 0);
        jTextCode.setText(of);
        jTextAreaGuideAfterDx.setText((String)jTable1.getValueAt(row, 1));
        Guide bgi = new Guide();
        for(int i =0 ; i< vGuide.size() ; i++)
        {           
             if(of == ((Guide)vGuide.get(i)).number)
                bgi = (Guide)vGuide.get(i);               
        }
        theGuide = bgi;       
    }      
    private void searchGuide()
    {   
        next = 0;
        prev = 0;
        String search = jTextFieldSCode.getText();
        String point = new String();
        vGuide =  theLookupControl.listGuideNCF(search);
       setTable(vGuide,1);        
    }
    private void setTable(Vector voffice,int off)
    {   
        String[] col = {"รหัส","คำแนะนำ"};        
        int count = offset;
        int p =0;
        int n =0;
        int c =0;
        if(voffice != null)
        {
            if(off == 1)
            {   
                p = prev;
                n = next;            
                prev = next;
                next = next + offset;
                if(next >= vGuide.size())
                {   
                    next = vGuide.size();
                    count = next - prev;
                }
                if(count == 0)
                {   
                    prev = p;
                    next = n;
                    count = n - p;
                }
            }
            else
            {   next = prev;
                prev = prev - offset;
                if(prev <=0)
                {    
                    prev = 0;
                    next = offset;
                }
                if(next >= vGuide.size())
                {   
                    next= vGuide.size();
                    count = next;
                }
            }
            TaBleModel tm = new TaBleModel(col,count);
            for(int i=0 ;i<count;i++)
            {  
                Guide of = (Guide)voffice.get(i+prev);           
               tm.setValueAt(of.number,i,0);
                tm.setValueAt(of.description,i,1);
            }
            jTable1.setModel(tm);            
        }
        else
        {   
            TaBleModel tm = new TaBleModel(col,0);
            jTable1.setModel(tm);
        }
         setTableListDefault();
    }   
