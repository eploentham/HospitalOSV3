    private void saveGuide()
   {  
       theGuide.number = Gutil.CheckReservedWords(jTextCode.getText());
       theGuide.description = Gutil.CheckReservedWords(jTextAreaGuideAfterDx.getText());        
       if(theGuide.number.equals("") || theGuide.description.equals(""))
       {
           theUS.setStatus("ไม่สามารถบันทึกข้อมูลเป็นค่าว่างได้",UpdateStatus.WARNING);       
           return ;
       }           
       Vector gad = theSetupControl.listGuideByDescription(theGuide.description);
        if(gad!=null &&  !gad.isEmpty())
       {
          theUS.setStatus("ไม่สามารถบันทึกข้อมูลซ้ำได้",UpdateStatus.WARNING);       
          return;
       }
        theSetupControl.saveGuide(theGuide);
       setEnableAll(false);
       searchGuide();
   }
