   private void selectVitalTemplate()
    {   setEnableAll(true);
        saved = 0;
        int row = jTable1.getSelectedRow();
        String of = (String)jTable1.getValueAt(row, 0);
        jTextCode.setText(of);
        jTextAreaVT.setText((String)jTable1.getValueAt(row, 1));
        VitalTemplate bgi = new VitalTemplate();
        for(int i =0 ; i< vitaltemplate.size() ; i++)
        {           
             if(of == ((VitalTemplate)vitaltemplate.get(i)).vital_template_id)
                bgi = (VitalTemplate)vitaltemplate.get(i);
               ComboboxModel.setCodeComboBox(jComboBoxServicePoint, bgi.service_point);
        }
        theVitalTemplate = bgi;
        
      /*  if(bgi.active.equals("1"))
            jCheckBox1.setSelected(true);
        else
            jCheckBox1.setSelected(false);*/
    }
      
    private void searchVitalTemplate()
    {   next = 0;
        prev = 0;
        String search = jTextFieldSCode.getText();
        String point = new String();
      if(!jCheckBoxSearch.isSelected())
      {
        point = "0"; 
      } 
      else
      {
         point = Gutil.getGuiData(jComboBoxServicePointSearch);  
      }
      vitaltemplate =  theSetupControl.listVitalTemplate(search,point);
      setTable(vitaltemplate,1);
    }
    private void setTable(Vector voffice,int off)
    {   
        VitalTemplate of = new VitalTemplate();
        int count = offset;
        int p =0;
        int n =0;
        int c =0;
        if(voffice != null)
        {
            if(off == 1)
            {   p = prev;
                n = next;
                prev = next;
                next = next + offset;
                if(next >= vitaltemplate.size())
                {   next = vitaltemplate.size();
                    count = next - prev;
                }
                if(count == 0)
                {   prev = p;
                    next = n;
                    count = n - p;
                }
            }
            else
            {   next = prev;
                prev = prev - offset;
                if(prev <=0)
                {    prev = 0;
                    next = offset;
                }
                if(next >= vitaltemplate.size())
                {   next= vitaltemplate.size();
                    count = next;
                }
            }
            TaBleModel tm = new TaBleModel(col,count);
            for(int i=0 ;i<count;i++)
            {  
                of = (VitalTemplate)voffice.get(i+prev);
                 tm.setValueAt(of.vital_template_id,i,0);
                tm.setValueAt(of.description,i,1);
            }
            jTable1.setModel(tm);
        }
        else
        {   TaBleModel tm = new TaBleModel(col,0);
            jTable1.setModel(tm);
        }
        jTable1.getColumnModel().getColumn(0).setPreferredWidth(60); // ชื่อสามัญสำหรับ รพ.ทั่วไป, ชื่อการค้า สำหรับศูนย์โรคตา
        jTable1.getColumnModel().getColumn(1).setPreferredWidth(300); // จำนวน
    }

    private void saveVT()
    {  
        theVitalTemplate.vital_template_id = Gutil.CheckReservedWords(jTextCode.getText());
        theVitalTemplate.description = Gutil.CheckReservedWords(jTextAreaVT.getText());
        theVitalTemplate.service_point = Gutil.getGuiData(jComboBoxServicePoint);

        if(theVitalTemplate.vital_template_id.equals("")
        || theVitalTemplate.description.equals("")){
            theUS.setStatus("ไม่สามารถบันทึกข้อมูลเป็นค่าว่างได้",UpdateStatus.WARNING);
            return;
        }
        VitalTemplate vt = theSetupControl.listVitalTemplateByCodeSP(
            theVitalTemplate.vital_template_id, theVitalTemplate.service_point);
        if(vt!=null && theVitalTemplate.getObjectId()==null){
            theUS.setStatus("ไม่สามารถบันทึกรหัสซ้ำได้",UpdateStatus.WARNING);
            return;
        }
        if(vt!=null && theVitalTemplate.getObjectId()!=null
        && !theVitalTemplate.getObjectId().equals(vt.getObjectId())){
            theUS.setStatus("ไม่สามารถบันทึกรหัสซ้ำได้",UpdateStatus.WARNING);
            return;
        }
        if(theVitalTemplate.getObjectId() == null)
            theSetupControl.saveVitalTemplate(theVitalTemplate);
        else
            theSetupControl.editVitalTemplate(theVitalTemplate);

        setEnableAll(false);
        searchVitalTemplate();
    }
