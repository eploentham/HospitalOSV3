    private void setTable(Vector voffice,int off)
    {   
        QueueVisit of = new QueueVisit();
        int count = offset;
        int p =0;
        int n =0;
        int c =0;
        if(voffice != null)
        {
            if(off == 1)
            {   p = prev;
                n = next;
                prev = next;
                next = next + offset;
                if(next >= QueueVisit.size())
                {   next = QueueVisit.size();
                    count = next - prev;
                }
                if(count == 0)
                {   prev = p;
                    next = n;
                    count = n - p;
                }
            }
            else
            {   next = prev;
                prev = prev - offset;
                if(prev <=0)
                {    prev = 0;
                    next = offset;
                }
                if(next >= QueueVisit.size())
                {   next= QueueVisit.size();
                    count = next;
                }
            }
            TaBleModel tm = new TaBleModel(col,count);
            for(int i=0 ;i<count;i++)
            {  
                of = (QueueVisit)voffice.get(i+prev);
                 tm.setValueAt(of.id,i,0);
                 tm.setValueAt(of.description,i,1);
                 tm.setValueAt(of.color + "|" + of.description, i, 2);
                 tm.setEditingCol(3);
                 tm.setValueAt(Boolean.valueOf(Gutil.isSelected(of.active)), i, 3);//Gutil.isSelected(of.active)
            }
            jTable1.setModel(tm);
        }
        else
        {   TaBleModel tm = new TaBleModel(col,0);
            jTable1.setModel(tm);
        }
         setTableListDefault();
    }    
