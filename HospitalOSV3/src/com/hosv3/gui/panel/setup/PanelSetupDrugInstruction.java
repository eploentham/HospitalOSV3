    private void deleteDrugInstructionc()
    {
        int ret = theSetupControl.deleteDrugInstruction(theDrugInstruction);
        if(ret >0)
        {
            theUS.setStatus("ลบวิธีการใช้ยาเสร็จสิ้น", theUS.COMPLETE);
            setEnableAll(false);
            clearAll();
            //pu : 25/08/2549 : เก็บ Index ปัจจุบันของหน้ารายการที่กำลังบันทึก
            int count = next - prev;
            this.curNext = next - count;
            this.curPrev = prev - offset;
            searchDrugInstruction();
        }
    }
