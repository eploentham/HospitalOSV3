    private void saveDrugInstruction()
    {
        int ret =0;
        theDrugInstruction.drug_instruction_id = jTextFieldCode.getText();
        theDrugInstruction.description = jTextFieldDescription.getText();
        if(jCheckBox1.isSelected())
            theDrugInstruction.active = "1";
        else
            theDrugInstruction.active = "0";
        if((!theDrugInstruction.drug_instruction_id.equals(""))&&(!theDrugInstruction.description.equals("")))
        {
            //DrugInstruction di = theSetupControl.listDrugInstructionByCode(theDrugInstruction.getObjectId());
            DrugInstruction di = theSetupControl.listDrugInstructionByCode(jTextFieldCode.getText().replace("'".charAt(0),' ').trim());
            if(di==null)
            {
                if(theDrugInstruction.getObjectId() == null)
                {
                    ret = theSetupControl.saveDrugInstruction(theDrugInstruction);
                }
                else
                {
                    ret = theSetupControl.editDrugInstruction(theDrugInstruction);
                }
                this.theUS.setStatus("บันทึกวิธีการใช้ยาเสร็จสิ้น", this.theUS.COMPLETE);
                setEnableAll(false);
            }
            else
            {
                if(theDrugInstruction.getObjectId()==null)
                {
                    jTextFieldCode.setText("");
                    jTextFieldCode.requestFocus();
                    this.theUS.setStatus("ไม่สามารถบันทึกรหัสซ้ำได้", this.theUS.WARNING);
                }
                else
                {
                    if(theDrugInstruction.getObjectId().equals(di.getObjectId()))
                    {
                        ret = theSetupControl.editDrugInstruction(theDrugInstruction);
                        this.theUS.setStatus("บันทึกวิธีการใช้ยาเสร็จสิ้น", this.theUS.COMPLETE);
                        setEnableAll(false);
                    }
                    else
                    {
                        jTextFieldCode.setText("");
                        jTextFieldCode.requestFocus();
                        this.theUS.setStatus("ไม่สามารถบันทึกรหัสซ้ำได้", this.theUS.WARNING);
                    }
                }
                di = null;
            }
        }
        else
        {
            this.theUS.setStatus("ไม่สามารถบันทึกข้อมูลเป็นค่าว่างได้", this.theUS.WARNING);
        }
        if(ret > 0)
        {
            //pu : 25/08/2549 : เก็บ Index ปัจจุบันของหน้ารายการที่กำลังบันทึก
            int count = next - prev;
            this.curNext = next - count;
            this.curPrev = prev - offset;
            searchDrugInstruction();
        }
    }
