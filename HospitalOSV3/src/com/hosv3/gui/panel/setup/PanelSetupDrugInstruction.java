    private void selectDrugInstruction()
    {
        setEnableAll(true);
        saved = 0;
        int row = jTable1.getSelectedRow();
        String of = (String)jTable1.getValueAt(row, 0);
        jTextFieldCode.setText(of);
        jTextFieldDescription.setText((String)jTable1.getValueAt(row, 1));
        DrugInstruction bgi = new DrugInstruction();
        for(int i =0 ; i< druginstruction.size() ; i++)
        {
            if(of == ((DrugInstruction)druginstruction.get(i)).drug_instruction_id)
                bgi = (DrugInstruction)druginstruction.get(i);
        }
        theDrugInstruction = bgi;
        if(bgi.active.equals("1"))
            jCheckBox1.setSelected(true);
        else    jCheckBox1.setSelected(false);
    }
    private void searchDrugInstruction()
    {   //pu : 25/08/2549 : กำหนดค่า Index ให้กับหน้าที่ต้องการแสดงรายการ
        next = this.curNext;
        prev =  this.curPrev;
        String search = jTextFieldSCode.getText();
        String active = "0";
        if(jCheckBoxS.isSelected())
            active = "1";
        druginstruction =  theSetupControl.listDrugInstructionByCNA(search, active);
        //if(druginstruction != null)
        setTable(druginstruction,1);
    }
    private void setTable(Vector voffice,int off)
    {   String[] col = {"รหัส","รายละเอียด"};
        DrugInstruction of = new DrugInstruction();
        //tableoffice = voffice;
        //    jTable1.removeAll();
        int count = offset;
        int p =0;
        int n =0;
        int c =0;
        if(voffice != null && voffice.size() != 0)
        {
            if(off == 1)
            {
                p = prev;
                n = next;
                prev = next;
                next = next + offset;
                if(next >= druginstruction.size())
                {   next = druginstruction.size();
                    count = next - prev;
                }
                if(count == 0)
                {   prev = p;
                    next = n;
                    count = n - p;
                }
            }
            else
            {   next = prev;
                prev = prev - offset;
                if(prev <=0)
                {    prev = 0;
                     next = offset;
                }
                if(next >= druginstruction.size())
                {   next= druginstruction.size();
                    count = next;
                }
            }
            TaBleModel tm = new TaBleModel(col,count);
            for(int i=0 ;i<count;i++)
            {
                of = (DrugInstruction)voffice.get(i+prev);
                tm.setValueAt(of.drug_instruction_id,i,0);
                tm.setValueAt(of.description,i,1);
            }
            jTable1.setModel(tm);
        }
        else
        {   TaBleModel tm = new TaBleModel(col,0);
            jTable1.setModel(tm);
        }
        setTableListDefault();
    }
