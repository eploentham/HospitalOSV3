    public DiseaseTemplate getDiseaseTemplate()
    {
        int row = this.jTableDiseaseTemplate.getSelectedRow();
        DiseaseTemplate dt = (DiseaseTemplate)vDiseaseTemplate.get(row);
        return dt;
    }

    public void setDiseaseTemplate(Vector voffice)
    {
        vDiseaseTemplate = voffice;
        if(vDiseaseTemplate==null)
            vDiseaseTemplate = new Vector();
        TaBleModel tm = new TaBleModel(col,vDiseaseTemplate.size());
        for(int i=0,size=vDiseaseTemplate.size();i<size;i++)
        {
            DiseaseTemplate pp = (DiseaseTemplate)vDiseaseTemplate.get(i);   
            tm.setValueAt(pp.name,i,0);    
        }
        jTableDiseaseTemplate.setModel(tm);
    } 
