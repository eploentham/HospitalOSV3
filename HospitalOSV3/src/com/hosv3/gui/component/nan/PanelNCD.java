    private boolean listHistory()
    {
        ht = new Hashtable();
        ht = theHC.theVisitControl.listNCD();
        // ถ้า ht ไม่เท่ากับ null sumo 25/08/2549
        if(ht == null)
        {
            return false;
        }
        String[] col_jTableItemList = (String[])ht.get("columnName");
        Vector v = (Vector)ht.get("value");
        setTableNCDHistory(col_jTableItemList,v);
        return true;
    }
    
    public void setTableNCDHistory(String[] col_jTableItemList,Vector v)
    {     
        DefaultTableModel tblModel = new DefaultTableModel();
        Constant.println("col_jTableItemList" + col_jTableItemList.length);
        for(int i=0; i<col_jTableItemList.length; i++)
        {
            Object[] colDatas = (Object[])v.get(i);
            tblModel.addColumn(col_jTableItemList[i] , colDatas);
        }
        jTableNCDHistory.setModel(tblModel);
        for(int i=0; i<col_jTableItemList.length; i++)
        {
            jTableNCDHistory.getColumnModel().getColumn(i).setPreferredWidth(103);
        }
   }
    
    public void showDialog()
    {
        boolean ret = listHistory();
        if(ret)
        {
            theJD.setVisible(true);
        }
        else
        {
            theJD.setVisible(false);
        }   
    }
    
