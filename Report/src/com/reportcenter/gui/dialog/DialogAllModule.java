    /**ใช้ในการกำหนดข้อมูลบนตาราง
     *@param vc เป็น Vector ของ Object Version 
     */
    private void setTable(Vector vc)
    {
        theTableModelGUI = new TableModelGUI(headColumn,0);
        if(vc !=null)
        {   int size = vc.size();
            theTableModelGUI = new TableModelGUI(headColumn,size);
            
            for(int i = 0 ;i < size;i++)
            {
                theVersion = (Version)vc.get(i);
                theTableModelGUI.setValueAt(theVersion.description,i,0);
                theTableModelGUI.setValueAt(theVersion.app_code,i,1);
                theTableModelGUI.setValueAt(theVersion.db_code,i,2);
                theTableModelGUI.setValueAt(theVersion.update_time,i,3);
                
                theVersion = null;
            }
        }
        
        this.jTableShowModule.setModel(theTableModelGUI);
        
        jTableShowModule.getColumnModel().getColumn(0).setPreferredWidth(150);
        jTableShowModule.getColumnModel().getColumn(1).setPreferredWidth(70);
        jTableShowModule.getColumnModel().getColumn(2).setPreferredWidth(150);
        jTableShowModule.getColumnModel().getColumn(3).setPreferredWidth(100);
        jTableShowModule.getColumnModel().getColumn(1).setCellRenderer(ColumnTableRenderer.getRendererCenter());
        jTableShowModule.getColumnModel().getColumn(2).setCellRenderer(ColumnTableRenderer.getRendererCenter());
    }
    
    /**ใช้ในการกำหนดภาษาให้กับ GUI*/
