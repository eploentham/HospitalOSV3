    /**
     * ลบรายการที่เลือกออกจากฐานข้อมูล
     * @Author Ojika
     * @Date 08/03/2549
     **/
    private void deleteEpiMeaslesItem()
    {
        if(this.theEpiMeaslesItem != null)
        {
            // ส่งค่าของ Object ไปลบ
            this.theHosManage.theHosControl.theEpiMeaslesItemControl.deleteEpiMeaslesItemByKeyId(this.theEpiMeaslesItem);
            // แจ้งบอกสถานะการลบรายการ         
            
            // เรียกค้นหาอีกครั้ง
            searchEpiMeaslesItem();
        }        
    }    
    
    /**
     * เลือกรายการวัคซีนของหัดเยอรมัน และ แสดงรายละเอียด
     * @Author Ojika
     * @Date 08/03/2549
     **/
     private void selectEpiMeaslesItem(int row)
     {
        if(row > -1)
        {
            // มีการเลือกจากตาราง
            if(this.vEpiMeaslesItem != null)
            {                
                this.theEpiMeaslesItem = (EpiMeaslesItem)vEpiMeaslesItem.get(row);
                // แสดงรายละเอียดข้อมูล    
                setObjectToData();
            }
        }
     }
     
     /**
      * แสดงค่าจาก Object ให้กับ GUI
      * @Author Ojika
      * @Date 08/03/2549
      **/
     private void setObjectToData()
     {
        if(this.theEpiMeaslesItem != null)
        {
            jTextFieldDescrpiton.setText(this.theEpiMeaslesItem.epi_measles_item_description);
        }
        else
        {
            clearGUI();
        }
     }
     
