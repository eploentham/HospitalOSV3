    /**
     * สำหรับค้นหารายการวัคซีนของหัดเยอรมัน และ ส่งข้อมูลไปแสดงในตาราง
     * @Author Ojika
     * @Date 08/03/2549
     **/
    private void searchEpiMeaslesItem()
    {
        // ค้นหารายการ โดยส่ง keyword ไปค้น
        this.vEpiMeaslesItem = new Vector();
        String keyword = jTextFieldSearch.getText().trim();
        
        this.vEpiMeaslesItem = this.theHosManage.theHosControl.theEpiMeaslesItemControl.searchEpiMeaslesItem(keyword);
        
        // ส่งข้อมูลที่ได้จากการค้นหานั้นไปแสดงยังตาราง
        setTableEpiMeaslesItem(this.vEpiMeaslesItem);
        
        this.theEpiMeaslesItem = null;
        clearGUI();
    }
    
    /**
     * แสดงข้อมูลที่ได้จากการค้นหาในตาราง
     * @author Ojika
     * @Date 08/03/2549
     **/
    private void setTableEpiMeaslesItem(Vector vData)
    {       
        theTableModelGUI = new TableModelGUI(colName, 0);
        if(vData != null)
        {
            int size = vData.size();
            theTableModelGUI = new TableModelGUI(colName, size);
            theEpiMeaslesItem = null;
            for(int i = 0 ; i < size ;i++)
            {
                theEpiMeaslesItem = (EpiMeaslesItem)vData.get(i);
                theTableModelGUI.setValueAt(theEpiMeaslesItem.epi_measles_item_description,i, 0);
                
                theEpiMeaslesItem = null;
            }
            
        }
        
        jTableShowList.setModel(theTableModelGUI);
       
    }   
    
    /**
     * รับ Vector ของ Epi ที่เลือกเพื่อ save เป็นรายการวัคซีนของหัดเยอรมัน
     * @Author Ojika
     * @Date 08/03/2549
     **/   
    public void notifySaveEpiMeaslesItem(Vector vEpi)
    {
        if(vEpi != null)
        {
            System.out.println(vEpi.size());
            Epi theEpi = new Epi();
            int size = vEpi.size();
            for(int i =0;i<size;i++)
            {

                theEpi = (Epi)vEpi.get(i);
                
                theEpiMeaslesItem = new EpiMeaslesItem();
                
                theEpiMeaslesItem.b_health_epi_group_id = theEpi.getObjectId();
                theEpiMeaslesItem.epi_measles_item_description = theEpi.health_epi_group_description;
                checkSameData(theEpiMeaslesItem);
          
                theEpiMeaslesItem = null;
            }
            theEpi = null;
        }
        
        //เรียก Function การค้นหาเพื่อเปลี่ยนข้อมูใหม่   
        searchEpiMeaslesItem();
    }
    
    private void checkSameData(EpiMeaslesItem theEMI)
    {
        if(this.vEpiMeaslesItem != null)
        {
            int size = this.vEpiMeaslesItem.size();
            if(size > 0)
            {
                for(int i =0;i<size;i++)
                {
                    if(!theEMI.b_health_epi_group_id.equals(((EpiMeaslesItem)this.vEpiMeaslesItem.get(i)).b_health_epi_group_id))
                    {
                        status = 1;
                    }
                    else
                    {
                        status = 0;
                        JOptionPane.showMessageDialog(this,Language.getTextBundle("Same_Item", language) + theEMI.epi_measles_item_description ,Language.getTextBundle("Warning", language),JOptionPane.WARNING_MESSAGE);
                        return;
                    }
                }
            }
        } 
        theHosManage.theHosControl.theEpiMeaslesItemControl.saveEpiMeaslesItem(theEMI);
        
    }
    
