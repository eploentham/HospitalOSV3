    /**
     * ใช้ในการ แสดงข้อมูลการค้นหาลงบนตารางที่กำหนด
     * @param vc เป็น Vector ของข้อมูลที่ทำการส่งมาให้และต้องการกำหนดให้แสดงบนตาราง
     */
    private void showDataToTable(Vector vc)
    {
        theTableModelGUI = new TableModelGUI(colName, 0);
        if(vc != null)
        {
            int size = vc.size();
            theTableModelGUI = new TableModelGUI(colName, size);
            theRp115Group4Item = null;
            for(int i = 0 ; i < size ;i++)
            {
                theRp115Group4Item = (Rp115Group4Item)vc.get(i);
                theTableModelGUI.setValueAt(theRp115Group4Item.rp11_item_number,i, 0);
                theTableModelGUI.setValueAt(theRp115Group4Item.rp11_item_description,i, 1);
                
                theRp115Group4Item = null;
            }
        }
        jTableSameData.setModel(theTableModelGUI);
        jTableSameData.getColumnModel().getColumn(0).setPreferredWidth(25);
        jTableSameData.getColumnModel().getColumn(1).setPreferredWidth(100);
    }
    
    
