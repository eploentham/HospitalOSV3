    /**ใช้ในการกำหนด ให้แสดงภาษาอะไร */
    public void setUseLanguage()
    {
        int row = this.jComboBoxLanguage.getSelectedIndex();
        Constant.println(this.jComboBoxLanguage.getSelectedIndex());
        if(row != -1)
        {   
            theFixLanguage = (FixLanguage)vFixLanguage.get(row);
            drc.setUseLanguage(theFixLanguage);
            System.exit(0);
        }
        
    }

    /**
     *  รับข้อมูลจาก Xml ไฟล์ เพื่อ show ให้ผู้ใช้เลือกใน combobox
     * @deprecated henbe unused DefaultReadConfig is depre
     */
    public void getDataSetLanguage()
    {
        drc = new DefaultReadConfig();
        vFixLanguage = null;
        boolean result = false;
        if(drc.checkXMLFile())
        {   
            drc.readXML();
            vFixLanguage = drc.getVector();
        }
        
         setComboBoxLanguage();
    }
    
    /**
     *  กำหนดภาษาลงใน combobox เพื่อให้ ผู้ใช้เลือก
     */
    public void setComboBoxLanguage()
    {
        if(vFixLanguage != null)
        {   
            int size = vFixLanguage.size();
            String[] value = new String[size];
            for(int i =0 ;i<size;i++)
            {
                theFixLanguage = (FixLanguage)vFixLanguage.get(i);
                value[i] = theFixLanguage.description;
            }
            jComboBoxLanguage.setModel(new javax.swing.DefaultComboBoxModel(value));
        }
        
        
    }
    
