    private void setTableRp505Group()
    {
        String[] col = {    Gutil.getTextBundle("GroupCode"),
                            Gutil.getTextBundle("GroupName"),
                            Gutil.getTextBundle("Male"),
                            Gutil.getTextBundle("Female"),
                            Gutil.getTextBundle("Non-Spec"),
                            Gutil.getTextBundle("Total")
                       };
        TableModel tm ;
        int size = 0;
        if(this.vcRp505Group != null)
        {
            size = this.vcRp505Group.size();
            tm= new TableModel(col,size);
            for(int i=0 ;i<size; i++)
            {
                tm.setValueAt(Integer.valueOf(((RP505Group)this.vcRp505Group.get(i)).group_rp505_number),i,0);
                tm.setValueAt(((RP505Group)this.vcRp505Group.get(i)).group_rp505_description_en,i,1);
                tm.setValueAt(Integer.valueOf(((RP505Group)this.vcRp505Group.get(i)).group_rp505_male),i,2);
                tm.setValueAt(Integer.valueOf(((RP505Group)this.vcRp505Group.get(i)).group_rp505_female),i,3);
                tm.setValueAt(Integer.valueOf(((RP505Group)this.vcRp505Group.get(i)).group_rp505_non_spec),i,4);
                tm.setValueAt(Integer.valueOf(((RP505Group)this.vcRp505Group.get(i)).group_rp505_total),i,5);
            }
        }
        else
        {
            tm= new TableModel(col,0);
        }
        this.jTableReport505Group.setModel(tm);
        setTableReportDefault();
        sendDataToMainReport(size);
    }
