    /**ใช้ในการ หยุด Process การทำงานของ การออกรายงาน 12 แฟ้ม*/
    public void StopProcess()
    {
        if(theHosManage != null)
        {
            theHosManage.theManageControl.theQueryControl.stop();
        }
        closeDialog();
    }
    
    /**ใช้ในการปิดการทำงานของโปรแกรม*/
