    /**ใช้ในการเปิดการทำงานของโปรแกรม*/
    public void showDialog(String status,boolean isFinish)
    {
        if(status != null && !("").equalsIgnoreCase(status))
        {
            this.jLabelStatus.setText(status);
        }
        jButtonStop.setText(Language.getTextBundle("Cancel", language));
        if(isFinish)
        {
            jButtonStop.setText(Language.getTextBundle("OK", language));
        }
        
        this.setVisible(true);
    }
    
    public void setEnableGUI(boolean setEnabled)
    {
        jButtonStop.setText(Language.getTextBundle("Cancel", language));
        if(!setEnabled)
        {
            jButtonStop.setText(Language.getTextBundle("OK", language));
        }
    }
    
