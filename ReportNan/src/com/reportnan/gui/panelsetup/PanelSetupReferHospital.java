    /**
     * สำหรับบันทึกรายการตรวจรักษา ที่เป็นหัตถการ
     * @Author Ojika
     * @Date 21/06/2549
     */
    private void saveReferHospital()
    {
        // นำข้อมูลบันทึกใน Object เพื่อส่งไปบันทึก
        setDataToObject();
        // สั่งบันทึกข้อมูล
        this.theMCS.theManageControl.theReferHospitalControl.saveReferHospital(this.theReferHospital);
        // ค้นหารายการตรวจรักษาของ OPD อีกครั้ง
        searchReferHospital();
        clearGUI();
    }
    
    /**
     * สำหรับบันทึกข้อมูลจาก GUI ให้กับ Object เพื่อเก็บค่าในการบันทึกหรือแก้ไข
     * @Author Ojika
     * @Date 21/06/2549
     */
    private void setDataToObject()
    {
        //ตรวจสอบว่าเป็นการแก้ไขหรือ บันทึกใหม่
        if(this.theReferHospital == null)
        {
            this.theReferHospital = new ReferHospital();
        }
        
        this.theReferHospital.refer_office_code = jTextFieldCode.getText().trim();
        this.theReferHospital.refer_office_description = jTextFieldDescription.getText().trim();
        
        // combobox
        this.theReferHospital.r_refer_office_type_id = this.theComboboxModel.getCodeComboBox(jComboBoxHospitalType);
    }
    
    /**
     * ลบรายการ ReferHospital ที่เลือกออกจากฐานข้อมูล
     * @Author Ojika
     * @Date 21/06/2549
     **/
    private void deleteReferHospital()
    {
        if(this.theReferHospital != null)
        {
            // ส่งค่าของ Object ไปลบ
            this.theMCS.theManageControl.theReferHospitalControl.deleteReferHospital(this.theReferHospital.getObjectId());        
            
            // เรียกค้นหาอีกครั้ง
            searchReferHospital();
        }        
    }    
    
    /**
     * เลือกรายการ ReferHospital
     * @Author Ojika
     * @Date 21/06/2549
     **/
     private void selectReferHospital(int row)
     {     
        if(row > -1)
        {
            // มีการเลือกจากตาราง
            if(this.vReferHospital != null && this.vReferHospital.size() > 0)
            {         
                this.theReferHospital = (ReferHospital)vReferHospital.get(row);
                // แสดงรายละเอียดข้อมูล    
                setObjectToData();
            }
        }
     }
     
     /**
      * แสดงค่าจาก Object ให้กับ GUI
      * @Author Ojika
      * @Date 21/06/2549
      **/
     private void setObjectToData()
     {         
        if(this.theReferHospital != null)
        {
            jTextFieldCode.setText(this.theReferHospital.refer_office_code);
            jTextFieldDescription.setText(this.theReferHospital.refer_office_description);
            
            // combobox
            this.theComboboxModel.setCodeComboBox(this.jComboBoxHospitalType, this.theReferHospital.r_refer_office_type_id);
        }
        else
        {
            clearGUI();
        }
     }
     
    /**
     * สำหรับค้นหารายการ ReferHospital เพื่อนำไปแสดงในตาราง
     * @Author ojika
     * @Date 21/06/2549
     **/
    private void searchReferHospital()
    {
        // ค้นหารายการ โดยส่ง keyword ไปค้น
        this.vReferHospital = new Vector();
        String keyword = jTextFieldSearch.getText().trim();
        
        this.vReferHospital = this.theMCS.theManageControl.theReferHospitalControl.searchReferHospitalByKeyword(keyword);
        
        // ส่งข้อมูลที่ได้จากการค้นหานั้นไปแสดงยังตาราง
        setTableReferHospital(this.vReferHospital);
        
        clearGUI();
    }
    
    /**
      * เคลียร์ข้อมูลบน Gui 
      * @Author Ojika
      * @Date 21/06/2549
      **/
     private void clearGUI()
     {
        this.theReferHospital = null;
        
        jTextFieldCode.setText("");
        jTextFieldDescription.setText("");
     }  
    
    /**
     * แสดงข้อมูลรายการ ReferHospital ที่ได้จากการค้นหาในตาราง
     * @author ojika
     * @Date 21/06/2549
     **/
    private void setTableReferHospital(Vector vData)
    {
        theTableModelGUI = new TableModelGUI(colName, 0);
        if(vData != null)
        {
            int size = vData.size();
            theTableModelGUI = new TableModelGUI(colName, size);
            theReferHospital = null;
            for(int i = 0 ; i < size ;i++)
            {
                theReferHospital = (ReferHospital)vData.get(i);
                theTableModelGUI.setValueAt(theReferHospital.refer_office_code,i, 0);
                theTableModelGUI.setValueAt(theReferHospital.refer_office_description,i, 1);
                theReferHospital = null;
            }
            
        }
        
        jTableReferHospital.setModel(theTableModelGUI);
        
        jTableReferHospital.getColumnModel().getColumn(0).setPreferredWidth(25);
        jTableReferHospital.getColumnModel().getColumn(1).setPreferredWidth(100);
    }
    
