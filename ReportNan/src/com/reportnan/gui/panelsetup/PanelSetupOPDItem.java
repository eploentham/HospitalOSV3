    /**
     * สำหรับบันทึกรายการตรวจรักษา OPD
     * @Author Ojika
     * @Date 21/06/2549
     */
    private void saveOPDItem()
    {
        // นำข้อมูลบันทึกใน Object เพื่อส่งไปบันทึก
        setDataToObject();
        // สั่งบันทึกข้อมูล
        if(jTextFieldName.getText().trim().equalsIgnoreCase(""))
        {
            JOptionPane.showMessageDialog(this,Language.getTextBundle("กรุณากรอกข้อมูล ชื่อที่จะนำไปแสดงในรายงานด้วย") ,Language.getTextBundle("Warning"),JOptionPane.WARNING_MESSAGE);
        }
        else
        {
            this.theMCS.theManageControl.theOPDItemControl.saveOPDItem(this.theOPDItem);
            // ค้นหารายการตรวจรักษาของ OPD อีกครั้ง
            searchOPDItem();
            clearGUI();
        }                
    }
    
    /**
     * สำหรับบันทึกข้อมูลจาก GUI ให้กับ Object เพื่อเก็บค่าในการบันทึกหรือแก้ไข
     * @Author Ojika
     * @Date 21/06/2549
     */
    private void setDataToObject()
    {
        //ตรวจสอบว่าเป็นการแก้ไขหรือ บันทึกใหม่
        if(this.theOPDItem == null)
        {
            this.theOPDItem = new OPDItem();
        }
        this.theOPDItem.b_item_id = opdItemId.trim();
        this.theOPDItem.opd_item_number = jTextFieldCode.getText().trim();
        this.theOPDItem.opd_item_common_name = jTextFieldCommonName.getText().trim();
        this.theOPDItem.opd_item_description = jTextFieldName.getText().trim();
    }
    
    /**
     * ลบรายการตรวจรักษา ของ OPD ที่เลือกออกจากฐานข้อมูล
     * @Author Ojika
     * @Date 21/06/2549
     **/
    private void deleteOPDItem()
    {
        if(this.theOPDItem != null)
        {
            // ส่งค่าของ Object ไปลบ
            this.theMCS.theManageControl.theOPDItemControl.deleteOPDItem(this.theOPDItem.getObjectId());        
            
            // เรียกค้นหาอีกครั้ง
            searchOPDItem();
        }        
    }    
    
    /**
     * เลือกกลุ่มรายการ NCD
     * @Author Ojika
     * @Date 21/06/2549
     **/
     private void selectOPDItem(int row)
     {     
        if(row > -1)
        {
            // มีการเลือกจากตาราง
            if(this.vOPDItem != null && this.vOPDItem.size() > 0)
            {         
                this.theOPDItem = (OPDItem)vOPDItem.get(row);
                // แสดงรายละเอียดข้อมูล    
                setObjectToData();
            }
        }
     }
     
     /**
      * แสดงค่าจาก Object ให้กับ GUI
      * @Author Ojika
      * @Date 21/06/2549
      **/
     private void setObjectToData()
     {         
        if(this.theOPDItem != null)
        {
            jTextFieldCode.setText(this.theOPDItem.opd_item_number);
            jTextFieldCommonName.setText(this.theOPDItem.opd_item_common_name);
            jTextFieldName.setText(this.theOPDItem.opd_item_description);
        }
        else
        {
            clearGUI();
        }
     }
     
    /**
     * สำหรับค้นหารายการตรวจรักษา OPD เพื่อนำไปแสดงในตาราง
     * @Author ojika
     * @Date 21/06/2549
     **/
    private void searchOPDItem()
    {
        // ค้นหารายการ โดยส่ง keyword ไปค้น
        this.vOPDItem = new Vector();
        String keyword = jTextFieldSearch.getText().trim();
        
        this.vOPDItem = this.theMCS.theManageControl.theOPDItemControl.searchOPDItemByKeyword(keyword);
        
        // ส่งข้อมูลที่ได้จากการค้นหานั้นไปแสดงยังตาราง
        setTableOPDItem(this.vOPDItem);
        
        clearGUI();
    }
    
    /**
      * เคลียร์ข้อมูลบน Gui 
      * @Author Ojika
      * @Date 21/06/2549
      **/
     private void clearGUI()
     {
        this.theOPDItem = null;
        
        jTextFieldCode.setText("");
        jTextFieldCommonName.setText("");
        jTextFieldName.setText("");
     }  
    
    /**
     * แสดงข้อมูลรายการตรวจรักษา OPD ที่ได้จากการค้นหาในตาราง
     * @author ojika
     * @Date 21/06/2549
     **/
    private void setTableOPDItem(Vector vData)
    {
        theTableModelGUI = new TableModelGUI(colName, 0);
        if(vData != null)
        {
            int size = vData.size();
            theTableModelGUI = new TableModelGUI(colName, size);
            theOPDItem = null;
            for(int i = 0 ; i < size ;i++)
            {
                theOPDItem = (OPDItem)vData.get(i);
                theTableModelGUI.setValueAt(theOPDItem.opd_item_number,i, 0);
                theTableModelGUI.setValueAt(theOPDItem.opd_item_description,i, 1);
                theOPDItem = null;
            }
            
        }
        
        jTableOPDItem.setModel(theTableModelGUI);
        
        jTableOPDItem.getColumnModel().getColumn(0).setPreferredWidth(25);
        jTableOPDItem.getColumnModel().getColumn(1).setPreferredWidth(100);
    }
    
