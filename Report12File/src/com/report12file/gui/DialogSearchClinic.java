    public void setSelectInRow(int[] rows)
    {   
        if(rows.length !=0)
        {   
            int size = rows.length;
            for(int i = 0 ; i < size ; i++)
            {
                Clinic theClinicTemp = (Clinic)vClinic.get(rows[i]);
                ClinicMap cm = new ClinicMap();
                cm.b_visit_clinic_id = theClinicTemp.getObjectId();
                cm.visit_clinic_number = theClinicTemp.clinic_id;
                cm.visit_clinic_description = theClinicTemp.name;
                vData.add(cm);
            }
        }
    }    
    
    public void searchData()
    {
        String keyword = this.jTextFieldSearch.getText().trim();
        vClinic = theHosControl.theClinicControl.selectClinicByKeyword(keyword); 
        showDataToTable(vClinic);
        
        keyword = null;
    }   
    
    /**
     * ใช้ในการ แสดงข้อมูลการค้นหาลงบนตารางที่กำหนด
     * @param vc เป็น Vector ของข้อมูลที่ทำการส่งมาให้และต้องการกำหนดให้แสดงบนตาราง
     */
    private void showDataToTable(Vector vc)
    {
        theTableModelGUI = new TableModelGUI(colName, 0);
        if(vc != null)
        {
            int size = vc.size();
            theTableModelGUI = new TableModelGUI(colName, size);
            for(int i = 0 ; i < size ;i++)
            {   
                Clinic theClinicTemp = (Clinic)vc.get(i);
                theTableModelGUI.setValueAt(theClinicTemp.clinic_id,i, 0);
                theTableModelGUI.setValueAt(theClinicTemp.name,i, 1);
            }            
        }        
        jTableShowList.setModel(theTableModelGUI);
        jTableShowList.getColumnModel().getColumn(0).setPreferredWidth(50);
        jTableShowList.getColumnModel().getColumn(1).setPreferredWidth(100);
    }
    
