    private void save() {
        this.theServiceType.number = this.jTextFieldNumber.getText();
        this.theServiceType.description = this.jTextFieldName.getText();
        if("".equalsIgnoreCase(theServiceType.number))
        {
            theUS.setStatus(GutilPCU.getTextBundle("กรุณาระบุลำดับ"), 2);
            return;
        }
        if("".equalsIgnoreCase(theServiceType.description))
        {
            theUS.setStatus(GutilPCU.getTextBundle("กรุณากรอกรายละเอียด"), 2);
            return;
        }
        if(this.jCheckBoxActive.isSelected())
            this.theServiceType.active = "1";
        else
            this.theServiceType.active = "0";
        
        //ตรวจสอบการเพิ่มข้อมูลซ้ำ ปู 20/12/2549
        Vector vList = new Vector();
        vList=  this.theSetupPcuControl.listServiceTypeByNameOrNumberAndActive("", this.theServiceType.active);
        for(int i=0,size = vList.size();i <size; i++)
        {
            ServiceType st = (ServiceType)vList.elementAt(i);
            if(this.theServiceType.getObjectId()==null
                    && this.theServiceType.description.equalsIgnoreCase(st.description))
            {
                theUS.setStatus("ชื่อประเภทการให้คำปรึกษานี้มีอยู่ในฐานข้อมูลแล้ว", 2);
                jTextFieldName.requestFocus();
                return;
            }
            else if(this.theServiceType.description.equalsIgnoreCase(st.description)
            &&!this.theServiceType.getObjectId().equalsIgnoreCase(st.getObjectId()))
            {
                theUS.setStatus("ชื่อประเภทการให้คำปรึกษานี้มีอยู่ในฐานข้อมูลแล้ว", 2);
                jTextFieldName.requestFocus();
                return;
            }
            if(this.theServiceType.getObjectId()==null
                    && this.theServiceType.number.equalsIgnoreCase(st.number))
            {
                theUS.setStatus("ลำดับนี้มีอยู่ในฐานข้อมูลแล้ว", 2);
                jTextFieldNumber.requestFocus();
                return;
            }
            else if(this.theServiceType.number.equalsIgnoreCase(st.number)
            &&!this.theServiceType.getObjectId().equalsIgnoreCase(st.getObjectId()))
            {
                theUS.setStatus("ลำดับนี้มีอยู่ในฐานข้อมูลแล้ว", 2);
                jTextFieldNumber.requestFocus();
                return;
            }
        }
        this.theSetupPcuControl.saveServiceType(this.theServiceType);
        this.theServiceType = null;
        
        setEnableGui(false);
        search();
    }
    
    private void search()
    {
        /***ค้นหาเขตที่ตั้งหมู่บ้าน***/
        String search = this.jTextFieldSearch.getText();
        String active = "0";
        if(this.jCheckBoxSearchActive.isSelected())
            active = "1";
        this.vServiceType =  this.theSetupPcuControl.listServiceTypeByNameOrNumberAndActive(search,active);
        
        /***SetTable***/
        String[] col = {GutilPCU.getTextBundle("Number"),
                GutilPCU.getTextBundle("Description")};
                
                ServiceType servicePlaceTemp = new ServiceType();
                TaBleModel tm;
                
                if(this.vServiceType != null)
                {
                    tm = new TaBleModel(col,this.vServiceType.size());
                    for(int i=0, size=this.vServiceType.size(); i<size; i++)
                    {
                        servicePlaceTemp = (ServiceType)this.vServiceType.get(i);
                        tm.setValueAt(servicePlaceTemp.number,i,0);
                        tm.setValueAt(servicePlaceTemp.description,i,1);
                    }
                }
                else
                {
                    tm= new TaBleModel(col,0);
                }
                
                servicePlaceTemp = null;
                this.jTableServiceType.setModel(tm);
                jTableServiceType.setRowHeight(20);
                /***SetTableDefault***/
                jTableServiceType.getColumnModel().getColumn(0).setPreferredWidth(80);
                jTableServiceType.getColumnModel().getColumn(1).setPreferredWidth(200);
    }
    
    private void select()
    {
        int row = this.jTableServiceType.getSelectedRow();
        this.theServiceType = (ServiceType)this.vServiceType.get(row);
        
        this.jTextFieldName.setText(this.theServiceType.description);
        this.jTextFieldNumber.setText(this.theServiceType.number);
        if(this.theServiceType.active.equals("1"))
            this.jCheckBoxActive.setSelected(true);
        else
            this.jCheckBoxActive.setSelected(false);
        
        setEnableGui(true);
    }
    
    private void clearGui()
    {
        this.jTextFieldNumber.setText("");
        this.jTextFieldName.setText("");
        this.jCheckBoxActive.setSelected(true);
    }
    
    private void delete()
    {
        this.theSetupPcuControl.deleteServiceType(this.theServiceType);
        this.theServiceType = null;
        
        setEnableGui(false);
        clearGui();
        search();
    }
    
    private void add()
    {
        clearGui();
        setEnableGui(true);
        this.theServiceType = new ServiceType();
    }
    
    
    
    
