    private void save() {
        this.theCheckHealthYearActivity.number = this.jTextFieldNumber.getText();
        this.theCheckHealthYearActivity.description = this.jTextFieldName.getText();
        if("".equalsIgnoreCase(theCheckHealthYearActivity.number))
        {
            theUS.setStatus(GutilPCU.getTextBundle("กรุณาระบุลำดับ"), 2);
            return;
        }
        if("".equalsIgnoreCase(theCheckHealthYearActivity.description))
        {
            theUS.setStatus(GutilPCU.getTextBundle("กรุณากรอกรายละเอียด"), 2);
            return;
        }
        if(this.jCheckBoxActive.isSelected())
            this.theCheckHealthYearActivity.active = "1";
        else
            this.theCheckHealthYearActivity.active = "0";
        
        //ตรวจสอบการเพิ่มข้อมูลซ้ำ ปู 20/12/2549
        Vector vList = new Vector();
        vList=  this.theSetupPcuControl.listCheckHealthYearActivityByNameOrNumberAndActive("", this.theCheckHealthYearActivity.active);
        for(int i=0,size = vList.size();i <size; i++)
        {
            CheckHealthYearActivity ch = (CheckHealthYearActivity)vList.elementAt(i);
            if(this.theCheckHealthYearActivity.getObjectId()==null
                    && this.theCheckHealthYearActivity.description.equalsIgnoreCase(ch.description))
            {
                theUS.setStatus("กิจกรรมการตรวจสุขภาพประจำปีนี้มีอยู่ในฐานข้อมูลแล้ว", 2);
                jTextFieldName.requestFocus();
                return;
            }
            else if(this.theCheckHealthYearActivity.description.equalsIgnoreCase(ch.description)
            &&!this.theCheckHealthYearActivity.getObjectId().equalsIgnoreCase(ch.getObjectId()))
            {
                theUS.setStatus("กิจกรรมการตรวจสุขภาพประจำปีนี้มีอยู่ในฐานข้อมูลแล้ว", 2);
                jTextFieldName.requestFocus();
                return;
            }
            if(this.theCheckHealthYearActivity.getObjectId()==null
                    && this.theCheckHealthYearActivity.number.equalsIgnoreCase(ch.number))
            {
                theUS.setStatus("ลำดับนี้มีอยู่ในฐานข้อมูลแล้ว", 2);
                jTextFieldNumber.requestFocus();
                return;
            }
            else if(this.theCheckHealthYearActivity.number.equalsIgnoreCase(ch.number)
            &&!this.theCheckHealthYearActivity.getObjectId().equalsIgnoreCase(ch.getObjectId()))
            {
                theUS.setStatus("ลำดับนี้มีอยู่ในฐานข้อมูลแล้ว", 2);
                jTextFieldNumber.requestFocus();
                return;
            }
            
        }
        this.theSetupPcuControl.saveCheckHealthYearActivity(this.theCheckHealthYearActivity);
        this.theCheckHealthYearActivity = null;
        
        setEnableGui(false);
        search();
    }
    
    private void search()
    {
        /***ค้นหาเขตที่ตั้งหมู่บ้าน***/
        String search = this.jTextFieldSearch.getText();
        String active = "0";
        if(this.jCheckBoxSearchActive.isSelected())
            active = "1";
        this.vCheckHealthYearActivity =  this.theSetupPcuControl.listCheckHealthYearActivityByNameOrNumberAndActive(search,active);
        
        /***SetTable***/
        String[] col = {GutilPCU.getTextBundle("Number"),
                GutilPCU.getTextBundle("Description")};
                
                CheckHealthYearActivity checkHealthYearActivity = new CheckHealthYearActivity();
                TaBleModel tm;
                
                if(this.vCheckHealthYearActivity != null)
                {
                    tm = new TaBleModel(col,this.vCheckHealthYearActivity.size());
                    for(int i=0, size=this.vCheckHealthYearActivity.size(); i<size; i++)
                    {
                        checkHealthYearActivity = (CheckHealthYearActivity)this.vCheckHealthYearActivity.get(i);
                        tm.setValueAt(checkHealthYearActivity.number,i,0);
                        tm.setValueAt(checkHealthYearActivity.description,i,1);
                    }
                }
                else
                {
                    tm= new TaBleModel(col,0);
                }
                
                checkHealthYearActivity = null;
                this.jTableCheckHealthYearActivity.setModel(tm);
                jTableCheckHealthYearActivity.setRowHeight(20);
                /***SetTableDefault***/
                jTableCheckHealthYearActivity.getColumnModel().getColumn(0).setPreferredWidth(80);
                jTableCheckHealthYearActivity.getColumnModel().getColumn(1).setPreferredWidth(200);
    }
    
    private void select()
    {
        int row = this.jTableCheckHealthYearActivity.getSelectedRow();
        this.theCheckHealthYearActivity = (CheckHealthYearActivity)this.vCheckHealthYearActivity.get(row);
        
        this.jTextFieldName.setText(this.theCheckHealthYearActivity.description);
        this.jTextFieldNumber.setText(this.theCheckHealthYearActivity.number);
        if(this.theCheckHealthYearActivity.active.equals("1"))
            this.jCheckBoxActive.setSelected(true);
        else
            this.jCheckBoxActive.setSelected(false);
        
        setEnableGui(true);
    }
    
