    private void saveVillageLocation()
    {
        this.theVillageLocation.number = this.jTextFieldVillageLocationNumber.getText();
        this.theVillageLocation.description = this.jTextFieldVillageLocationName.getText();
        if("".equalsIgnoreCase(theVillageLocation.number ))
        {
            theUS.setStatus(GutilPCU.getTextBundle("กรุณาระบุลำดับ"), 2);
            return;
        }
        if("".equalsIgnoreCase(theVillageLocation.description))
        {
            theUS.setStatus(GutilPCU.getTextBundle("กรุณากรอกรายละเอียด"), 2);
            return;
        }
        if(this.jCheckBoxActive.isSelected())
            this.theVillageLocation.active = "1";
        else
            this.theVillageLocation.active = "0";
        
        //ตรวจสอบการเพิ่มข้อมูลซ้ำ ปู 20/12/2549
        Vector vList = new Vector();
        vList=  this.theSetupPcuControl.listVillageLocationByNameOrNumberAndActive("", this.theVillageLocation.active);
        for(int i=0,size = vList.size();i <size; i++)
        {
            VillageLocation vl = (VillageLocation)vList.elementAt(i);
            if(this.theVillageLocation.getObjectId()==null
                    && this.theVillageLocation.description.equalsIgnoreCase(vl.description))
            {
                theUS.setStatus("เขตที่ตั้งของหมู่บ้านนี้มีอยู่ในฐานข้อมูลแล้ว", 2);
                jTextFieldVillageLocationName.requestFocus();
                return;
            }
            else if(this.theVillageLocation.description.equalsIgnoreCase(vl.description)
            &&!this.theVillageLocation.getObjectId().equalsIgnoreCase(vl.getObjectId()))
            {
                theUS.setStatus("เขตที่ตั้งของหมู่บ้านนี้มีอยู่ในฐานข้อมูลแล้ว", 2);
                jTextFieldVillageLocationName.requestFocus();
                return;
            }
            if(this.theVillageLocation.getObjectId()==null
                    && this.theVillageLocation.number.equalsIgnoreCase(vl.number))
            {
                theUS.setStatus("ลำดับนี้มีอยู่ในฐานข้อมูลแล้ว", 2);
                jTextFieldVillageLocationNumber.requestFocus();
                return;
            }
            else if(this.theVillageLocation.number.equalsIgnoreCase(vl.number)
            &&!this.theVillageLocation.getObjectId().equalsIgnoreCase(vl.getObjectId()))
            {
                theUS.setStatus("ลำดับนี้มีอยู่ในฐานข้อมูลแล้ว", 2);
                jTextFieldVillageLocationNumber.requestFocus();
                return;
            }
        }
        this.theSetupPcuControl.saveVillageLocation(this.theVillageLocation);
        this.theVillageLocation = null;
        
        setEnableGui(false);
        searchVillageLocation();
    }
    
