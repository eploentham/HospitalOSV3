    private void searchVillageLocation()
    {
        /***ค้นหาเขตที่ตั้งหมู่บ้าน***/
        String search = this.jTextFieldSearch.getText();  
        String active = "0";
        if(this.jCheckBoxSearchActive.isSelected())
            active = "1";
        this.vVillageLocation =  this.theSetupPcuControl.listVillageLocationByNameOrNumberAndActive(search,active);
        
        /***SetTable***/
        String[] col = {GutilPCU.getTextBundle("Number"),
                    GutilPCU.getTextBundle("Description")};
                    
        VillageLocation villageLocationTemp = new VillageLocation();
        TaBleModel tm;
        
        if(this.vVillageLocation != null)
        {   
            tm = new TaBleModel(col,this.vVillageLocation.size());
            for(int i=0, size=this.vVillageLocation.size(); i<size; i++)
            {  
                villageLocationTemp = (VillageLocation)this.vVillageLocation.get(i);
                tm.setValueAt(villageLocationTemp.number,i,0); 
                tm.setValueAt(villageLocationTemp.description,i,1);                
            }
        }
        else
        {   
            tm= new TaBleModel(col,0);
        } 
        
        villageLocationTemp = null;
        this.jTableVillageLocation.setModel(tm);  
        jTableVillageLocation.setRowHeight(20);
        /***SetTableDefault***/
        jTableVillageLocation.getColumnModel().getColumn(0).setPreferredWidth(80);      /*รหัสเขตที่ตั้งหมู่บ้าน*/
        jTableVillageLocation.getColumnModel().getColumn(1).setPreferredWidth(200);     /*ชื่อเขตที่ตั้ง*/        
    }
    
    private void selectVillageLocation()
    {
        int row = this.jTableVillageLocation.getSelectedRow();
        this.theVillageLocation = (VillageLocation)this.vVillageLocation.get(row); 
        
        this.jTextFieldVillageLocationName.setText(this.theVillageLocation.description);
        this.jTextFieldVillageLocationNumber.setText(this.theVillageLocation.number);
        if(this.theVillageLocation.active.equals("1"))
            this.jCheckBoxActive.setSelected(true);
        else
            this.jCheckBoxActive.setSelected(false); 
        
        setEnableGui(true);
    }
    
    private void deleteVillageLocation()
    {
        if(theLookupControl.readOption().life.equals(Active.isEnable())) {
            theHM.theHosControl.theUS.setStatus(Constant.getTextBundle("ระบบเริ่มใช้งานจริงแล้ว") +
                    " " +
                    Constant.getTextBundle("ไม่สามารถลบข้อมูลได้"),UpdateStatus.WARNING);
            return;
        }
        this.theSetupPcuControl.deleteVillageLocation(this.theVillageLocation);
        this.theVillageLocation = null;
        
        setEnableGui(false);
        clearGuiVillageLocation();
        searchVillageLocation();
    }
    
    private void addVillageLocation()
    {
        clearGuiVillageLocation();
        setEnableGui(true);
        this.theVillageLocation = new VillageLocation();
    }
    
