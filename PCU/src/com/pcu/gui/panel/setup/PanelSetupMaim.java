    private void jTextFieldNumberFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTextFieldNumberFocusLost
        boolean repeate = theSetupPcuControl.checkMaimNumber(jTextFieldNumber.getText()); 
        if(jTextFieldNumber.getText().equals(""))
        {
            theSetupPcuControl.setStatus("กรุณากรอกรหัส", com.hosv3.utility.connection.UpdateStatus.WARNING);
//            jTextFieldNumber.setSelectionStart(0);
//            jTextFieldNumber.requestFocus(); 
        }
        else if(repeate)
        {
            theSetupPcuControl.setStatus("กรุณากรอกรหัสอีกครั้ง เนื่องจากรหัสซ้ำกัน", com.hosv3.utility.connection.UpdateStatus.WARNING);
            jTextFieldNumber.setSelectionStart(0);
            jTextFieldNumber.requestFocus();
        }
    }//GEN-LAST:event_jTextFieldNumberFocusLost

