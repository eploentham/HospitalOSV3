    /**
     * บันทึกขึ้อมูล
     * @param  Vector vMaimType,int off
     * @return 
     * @author jao
     * @date 27-02-2549
     */
    
    private void save()
    {
        if(theMaimType==null)
            theMaimType = new MaimType();
        this.theMaimType.maim_number = this.jTextFieldNumber.getText();
        this.theMaimType.description = this.jTextFieldDescription.getText();
        if("".equalsIgnoreCase(theMaimType.maim_number))
        {
            theHM.theHosInf.setStatus(GutilPCU.getTextBundle("กรุณากรอกรหัส"), 2);
            return;
        }
        if("".equalsIgnoreCase(theMaimType.description))
        {
            theHM.theHosInf.setStatus(GutilPCU.getTextBundle("กรุณากรอกรายละเอียด"), 2);
            return;
        }
        if(this.jCheckBoxActive.isSelected())
            this.theMaimType.active = "1";
        else
            this.theMaimType.active = "0";
        
        //ตรวจสอบการเพิ่มข้อมูลซ้ำ ปู 20/12/2549
        Vector vList = new Vector();
        vList=  this.theSetupPcuControl.listMaimTypeByNameOrNumberAndActive("", this.theMaimType.active);
        for(int i=0,size = vList.size();i <size; i++)
        {
            MaimType mt = (MaimType)vList.elementAt(i);
            if(this.theMaimType.getObjectId()==null
                    && this.theMaimType.description.equalsIgnoreCase(mt.description))
            {
                theHM.theHosInf.setStatus("ชื่อกลุ่มความพิการนี้มีอยู่ในฐานข้อมูลแล้ว", 2);
                jTextFieldDescription.requestFocus();
                return;
            }
            else if(this.theMaimType.description.equalsIgnoreCase(mt.description)
            &&!this.theMaimType.getObjectId().equalsIgnoreCase(mt.getObjectId()))
            {
                theHM.theHosInf.setStatus("ชื่อกลุ่มความพิการนี้มีอยู่ในฐานข้อมูลแล้ว", 2);
                jTextFieldDescription.requestFocus();
                return;
            }
            if(this.theMaimType.getObjectId()==null
                    && this.theMaimType.maim_number.equalsIgnoreCase(mt.maim_number))
            {
                theHM.theHosInf.setStatus("ลำดับนี้มีอยู่ในฐานข้อมูลแล้ว", 2);
                jTextFieldNumber.requestFocus();
                return;
            }
            else if(this.theMaimType.maim_number.equalsIgnoreCase(mt.maim_number)
            &&!this.theMaimType.getObjectId().equalsIgnoreCase(mt.getObjectId()))
            {
                theHM.theHosInf.setStatus("ลำดับนี้มีอยู่ในฐานข้อมูลแล้ว", 2);
                jTextFieldNumber.requestFocus();
                return;
            }            
        }        
        int result = this.theSetupPcuControl.saveMaimType(this.theMaimType);        
        
        //setEnableGui(false);
        search();
    }    
    
