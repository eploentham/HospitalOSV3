     /**
     * แสดงข้อมูลในตาราง
     * @param  Vector vMaimType,int off
     * @return 
     * @author jao
     * @date 27-02-2549
     */
    private void setTable(Vector vMaimType,int off)
    {   
        count = offset;
        int p =0;
        int n =0;
        int c =0;
        if(vMaimType != null)
        {
            if(off == 1)
            {   p = prev;
                n = next;
                prev = next;
                next = next + offset;
                if(next >= vMaimType.size())
                {   next = vMaimType.size();
                    count = next - prev;
                }
                if(count == 0)
                {   prev = p;
                    next = n;
                    count = n - p;
                }
            }
            else
            {   next = prev;
                prev = prev - offset;
                if(prev <=0)
                {    prev = 0;
                    next = offset;
                }
                if(next >= vMaimType.size())
                {   next= vMaimType.size();
                    count = next;
                }
            }
        }
        /***SetTable***/
        String[] col = {GutilPCU.getTextBundle("Number"),
                    GutilPCU.getTextBundle("Description")};
                    
        MaimType MaimTypeTemp = new MaimType();
        TaBleModel tm;
        
        if(this.vMaimType != null)
        {   
            tm = new TaBleModel(col,count);
            for(int i=0, size=this.vMaimType.size(); i<size; i++)
            {  
                MaimTypeTemp = (MaimType)this.vMaimType.get(i);
                tm.setValueAt(MaimTypeTemp.maim_number,i,0); 
                tm.setValueAt(MaimTypeTemp.description,i,1);                
            }
        }
        else
        {   
            tm= new TaBleModel(col,0);
        } 
        
        MaimTypeTemp = null;
        this.jTableMaimType.setModel(tm);  
        jTableMaimType.setRowHeight(20);
        /***SetTableDefault***/
        jTableMaimType.getColumnModel().getColumn(0).setPreferredWidth(80);      
        jTableMaimType.getColumnModel().getColumn(1).setPreferredWidth(200);             
    }
    
    
