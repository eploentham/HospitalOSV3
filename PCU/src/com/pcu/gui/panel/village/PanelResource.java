    protected void updateResource()
    {
        String code = jTextFieldResourceCode.getText();
        if(code.equals(""))
        {  
            theUS.setStatus(GutilPCU.getTextBundle("FillNumber"),UpdateStatus.WARNING);
            return;
        }
        /*Resource*/
        theResource.resource_number = code;

        /*ชื่อทรัพยากร*/
        if(("").equals(Gutil.getGuiData(jComboBoxResource)))                
        {/*check ซ้ำ*/          
            String resourceName = "";
            resourceName = ((JTextField)(jComboBoxResource.getEditor()).getEditorComponent()).getText();                
            theCommunityResource = new CommunityResource();
            theCommunityResource = theVillageControl.readResourceName(resourceName);
            if(theCommunityResource!=null)
            {/*ซ้ำ*/
                Gutil.setGuiData(jComboBoxResource,theCommunityResource.getObjectId());
                theResource.resource_name = theCommunityResource.getObjectId(); 
                theCommunityResource = null;
            }
            else
            {/*ไม่ซ้ำ*/
                theCommunityResource = new CommunityResource();
                theCommunityResource.description = resourceName;                                      
                theCommunityResource.active = "1";
                theSetupPcuControl.saveResourceGui(theCommunityResource,null);
                theCommunityResource = null;
            }
        }
        else
        {
            theResource.resource_name = Gutil.getGuiData(jComboBoxResource);
        }

        theResource.village_id = Gutil.getGuiData(jComboBoxVillageResource);

        /*ResourceHistory*/
        theResourceHistory.resorce_history_owner_id = Gutil.getGuiData(jComboBoxOwnerResource);
        theResourceHistory.resource_history_owner_description = jTextFieldOwnerResourceDescription.getText();            
        theResourceHistory.resorce_history_supervise_id = Gutil.getGuiData(jComboBoxSuperViseResource);            
        theResourceHistory.resource_history_supervise_description = jTextFieldSuperViseResource.getText();
        theResourceHistory.resource_history_home_number = jTextFieldHomeNumberResource.getText();
        theResourceHistory.resource_history_moo = integerTextFieldMooResource.getText();
        theResourceHistory.resource_history_road = jTextFieldRoadResource.getText();
        theResourceHistory.resource_history_phone = jTextFieldPhoneResource.getText();            
        theResourceHistory.resource_history_changwat = panelCatAddress1.getChangwat();
        theResourceHistory.resource_history_amphur = panelCatAddress1.getAmpur();
        theResourceHistory.resource_history_tambol = panelCatAddress1.getTambon();
        if(jRadioButtonResourceNotWork.isSelected())
        {
            theResourceHistory.resource_history_state = PcuAnswer.Zero();
        }
        else
        {
            theResourceHistory.resource_history_state = PcuAnswer.One();
        }
        theResourceHistory.f_health_community_standard_type_id = Gutil.getGuiData(jComboBoxStandardTypeResource);            
        theResourceHistory.resource_history_standard_type_description = jTextFieldStandardTypeResourceDescription.getText();
        theResourceHistory.f_health_community_standard_id = Gutil.getGuiData(jComboBoxStandardResource);            
        theResourceHistory.resource_history_time_of_pass = integerTextFieldTimeOfPassResource.getText();
        theResourceHistory.resource_history_date_of_pass = dateComboBoxDateOfPassResource.getText();            
        theResourceHistory.resource_history_period_of_pass = jTextFieldPeriodOfTimeResource.getText();                         

        boolean ret = theVillageControl.updateResourceAndHistory(theResource,theResourceHistory); 
        if(!ret)
            return;

        int row = this.jTableResource.getSelectedRow();
        this.searchResource();
        if(row==-1)
            row = jTableResource.getRowCount()-1;
        jTableResource.setRowSelectionInterval(row,row);
        this.jTableResourceMouseReleased(null);
    }
    
