    protected void setTableResourceHistory()
    {
        String[] col = {GutilPCU.getTextBundle("DateRecord")};      
        TaBleModel tm ;
        
        if(vResourceHistory != null)
        {   
            int size = vResourceHistory.size()-1;
            tm = new TaBleModel(col,size);
            for(int i=0; i<size; i++)
            {  
                ResourceHistory resourceHistoryTemp = (ResourceHistory)vResourceHistory.get(i);
                if(i==0)
                    this.theResourceHistory = resourceHistoryTemp;
                if(!("").equals(resourceHistoryTemp.resource_history_record_date_time))
                    tm.setValueAt(GutilPCU.changDateToString(resourceHistoryTemp.resource_history_record_date_time,true),i,0);                
                else
                    tm.setValueAt(GutilPCU.changDateToString(theResource.resource_modify_date_time,true),i,0);
            }
        }
        else
        {   
            tm= new TaBleModel(col,0);
        } 
        jTableHistoryResource.setModel(tm);                       
    }
    
