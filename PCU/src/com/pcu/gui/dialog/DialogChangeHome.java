    private void jButtonSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSaveActionPerformed

        if (theUS.confirmBox("กรุณายืนยันการย้ายบ้าน?",UpdateStatus.WARNING))
        {
            theFamily.move_in_date_time = dateComboBoxDateMove.getText();
            theFamily.home_id = theHome.getObjectId();
            theFamily.modify_date_time = Gutil.getTextCurrentDateTime(theHC.theConnectionInf);
            int result = theHC.theHomeControl.savePatientInFamily(theFamily);
            if(result == 1)
            {
                theHM.theHosSubject.theHomePatientSubject.notifyChangeHome("บันทึกการย้ายบ้านเสร็จสิ้น", theHC.theUS.COMPLETE);
            }
        }        
        dispose();
    }//GEN-LAST:event_jButtonSaveActionPerformed

