    public void setBornMchV(Vector bornMchV)
    {
        TaBleModel tm;
        theBornMchV = bornMchV;
        if(theBornMchV==null)
            theBornMchV = new Vector();
        tm = new TaBleModel(col,theBornMchV.size());

        for(int i=0;i<theBornMchV.size();i++)
        {
            BornMch bornMch = (BornMch) theBornMchV.get(i);
            for(int i2=0;i2<thePregnancyV.size();i2++)
            {
                Pregnancy pregnancy = (Pregnancy) thePregnancyV.get(i2);
                if(pregnancy.pregnancy_gravida_number.equals(bornMch.gravida))
                {
                    tm.setValueAt(pregnancy.pregnancy_menses_issue_date, i, 1);
                    tm.setValueAt(pregnancy.pregnancy_menses_expire_date, i, 2);
                }
            }
            tm.setValueAt(bornMch.gravida, i, 0);
        }
        jTable1.setModel(tm);
    }
