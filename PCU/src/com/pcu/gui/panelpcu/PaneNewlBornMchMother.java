    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int select = jTable1.getSelectedRow();
        BornMch bornMch = null;
        if(select>=0)
        {
            if(this.theBornMchV!=null&&!this.theBornMchV.isEmpty())
            {
                bornMch = (BornMch) theBornMchV.get(select);
            }
        }
        int anc_time = 0;
        if(thePanelChooseBornMchMother == null)
        {
            thePanelChooseBornMchMother = new PanelChooseBornMchMother();
            thePanelChooseBornMchMother.setControl(theHM, theHD, theUS);
        }
        //Pregnancy pregnancy,BornMch bornMch,AncPcu ancPcu

        if(thePregnancyV!=null&&!thePregnancyV.isEmpty())
        {
            Pregnancy pregnancy = (Pregnancy) thePregnancyV.get(thePregnancyV.size()-1);
            Vector anc = theHM.theHosControl.theHealthServiceControl.listAncByPregnantID(pregnancy.getObjectId());
            AncPcu ancPcu = null;
            if(anc!=null&&!anc.isEmpty())
                ancPcu = (AncPcu) anc.get(anc.size()-1);
            anc_time = anc.size();
            thePanelChooseBornMchMother.setLeftPanel(pregnancy, bornMch, ancPcu);
        }
        thePanelChooseBornMchMother.setAncTime(anc_time, 3);
        thePanelChooseBornMchMother.showDialog();
    }//GEN-LAST:event_jButton1ActionPerformed


