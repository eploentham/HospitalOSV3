    public void updateOGToBeOne(ToBeOne toBeOne) {
        if (toBeOne.is_member.equals("1")) {
            this.jCheckBox1.setSelected(true);
            this.jCheckSchool.setEnabled(true);
            this.dateComboBox1.setEnabled(true);
            if (!toBeOne.workPlace.equals("")) {
                this.jCheckSchool.setSelected(true);
                this.jComboBoxSchool.setEnabled(true);
                ComboboxModel.setCodeComboBox(this.jComboBoxSchool, toBeOne.workPlace);
            } else {
                this.jCheckSchool.setSelected(false);
                this.jComboBoxSchool.setEnabled(false);
            }            
            this.jComboBoxReason.setEnabled(true);
            this.jTextPaneOpinion.setEnabled(true);
            this.dateComboBox1.setText(DateUtil.convertFieldDate(toBeOne.regisDate));            
            ComboboxModel.setCodeComboBox(this.jComboBoxReason, toBeOne.reason);
            this.jTextPaneOpinion.setText(toBeOne.opinion);
        } 
        else {
            this.jCheckBox1.setSelected(false);
            this.dateComboBox1.setEnabled(false);
            this.jComboBoxSchool.setEnabled(false);
            this.jComboBoxReason.setEnabled(false);
            this.jTextPaneOpinion.setEnabled(false);
            this.jCheckSchool.setEnabled(false);
            this.dateComboBox1.setText(DateUtil.getDateFromPattern("dd/MM/yyyy"));
            //this.jComboBoxSchool.setSelectedIndex(0);
            //this.jComboBoxReason.setSelectedIndex(0);
            this.jTextPaneOpinion.setText("");
        }
    }
    
    public void updateGOTobeOne(ToBeOne toBeOne) {
        toBeOne.family_id = theFamily.getObjectId();
        if (this.jCheckBox1.isSelected()) {
            toBeOne.is_member = Active.isEnable();
            toBeOne.regisDate = this.dateComboBox1.getText();
            if (jCheckSchool.isSelected()) {
                toBeOne.workPlace = Gutil.getGuiData(this.jComboBoxSchool);
            } else {
                toBeOne.workPlace = "";
            }
            
            toBeOne.reason = Gutil.getGuiData(this.jComboBoxReason);
            toBeOne.opinion = this.jTextPaneOpinion.getText();
        }
        else {
            toBeOne.is_member = Active.isDisable();
            toBeOne.regisDate = "";
            toBeOne.workPlace = "";
            toBeOne.reason = "";
            toBeOne.opinion = "";
        }
    }
    
    public void setControl(HtControl ht,UpdateStatus us){
         theUS = us;
        theHtControl = ht;
        ComboboxModel.initComboBox(this.jComboBoxSchool, theHtControl.listSchool());
        ComboboxModel.initComboBox(this.jComboBoxReason, theHtControl.listReason());
        this.dateComboBox1.setEditable(true);
        setDisable();
    }
    
    private void setDisable() {        
        this.dateComboBox1.setEnabled(false);
        this.jCheckSchool.setEnabled(false);
        this.jComboBoxSchool.setEnabled(false);
        this.jComboBoxReason.setEnabled(false);
        this.jTextPaneOpinion.setEnabled(false);     
    }
    
    private void setEnable() {        
        this.dateComboBox1.setEnabled(true);
        this.jCheckSchool.setEnabled(true);
        if (this.jCheckSchool.isSelected())
            this.jComboBoxSchool.setEnabled(true);
        else
            this.jComboBoxSchool.setEnabled(false);
        this.jComboBoxReason.setEnabled(true);
        this.jTextPaneOpinion.setEnabled(true);       
    }
    
    public void updateOGToBeOne(Vector v) {
        Vector p = v;
        theToBeOne = new ToBeOne();
        if (v==null)
        {
            v = new Vector();                
        }
        else
        {
            for (int i=0 ; i<v.size() ; i++) 
            {
                theToBeOne = (ToBeOne)v.get(i);            
            }
        }
        updateOGToBeOne(theToBeOne);
    }
    
    public void updateFamily(Family family) {
        theFamily = family;
        Vector familyV = theHtControl.listToBeOneByFid(family.getObjectId());
        updateOGToBeOne(familyV);
   }
    
    public void showDialog(Family family) {
        if(jDialog==null)
        {        
            if(theHtControl.theUS!=null)
                jDialog = new JDialog(this.theHtControl.theUS.getJFrame(),true);
            else
                jDialog = new JDialog();
            jDialog.getContentPane().add(this);
            jDialog.setSize(650, 195);
            Dimension screenSize = Toolkit.getDefaultToolkit().getScreenSize();
            jDialog.setLocation((screenSize.width-jDialog.getSize().width)/2
                    , (screenSize.height-jDialog.getSize().height)/2);
        }
        updateFamily(family);
        jDialog.setVisible(true);
    }
    
